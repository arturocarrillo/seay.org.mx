        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">Tres jóvenes universitarias realizan animación anticorrupción.</h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Miriam Tello Villamil, Sofía Piña Sauri y Cecilia Márquez Febles, estudiantes del 2º semestre de la licenciatura de Diseño Multimedia de la Escuela de Diseño de la Universidad Anáhuac-Mayab, son las autoras de una animación digital que sobre el tema anticorrupción realizaron como parte de un proyecto académico de la materia de Emprendimiento.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          “Platicando con mi mamá  -comentó Cecilia, hija de Mónica Febles Alvarez-Icaza presidente actual del Comité de Participación Ciudadana (CPC)-  surgió la idea de enfocar nuestro proyecto de animación de video a la difusión del Mecanismo de Queja. Presentamos a la escuela la iniciativa y fue aprobado como uno de los 10 proyectos piloto del programa U Challenge”
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          “Esta animación digital abre brecha”, comentó Javier Montes de Oca Zentella, encargado por el CPC de coordinarse con el equipo de producción, “es el primer trabajo de jóvenes estudiantes que de manera oficial cumple un requisito académico de su escuela y es a la vez un proyecto anticorrupción emanado del CPC. Agradecemos a la Universidad Anáhuac-Mayab su participación e invitamos a más jóvenes y más universidades a sumar sus talentos en proyectos de comunicación con temas anticorrupción”.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          La animación, de 55 segundos de duración, inicia preguntando en un texto escrito: “Cuándo haces un trámite gubernamental te piden una ayuda?” mientras se ve una mano deslizando un fajo de billetes y otra entregando un documento. 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          “¿Has visto que le han dado recursos a alguien que no los necesita? ¿No te promueven pero al primo flojo de tu jefe sí?”, sigue preguntando la animación para de inmediato invitar a utilizar el Mecanismo de Queja del Sistema Estatal Anticorrupción como “una herramienta ciudadana para combatir actos de corrupción.” 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Concluye el video con la invitación de “forma parte de la cultura anticorrupción porque tu queja ayuda al cambio que necesitas tú y el que necesitamos mucho más personas.Terminemos con el cáncer de la corrupción”
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          El video se difundirá a través de las redes sociales del CPC y del SEAY y se invita a los jóvenes a compartirlo en sus grupos para lograr una máxima publicidad.
        </p>
