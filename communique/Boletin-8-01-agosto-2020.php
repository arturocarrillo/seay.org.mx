      <div class="w-100 d-flex flex-wrap justify-content-center mt-5 p-2">
        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Posicionamiento en materia de contrataciones públicas del sector salud, ante la adición del párrafo quinto al artículo 1ero de la Ley de Adquisiciones, Arrendamientos y Servicios
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Las y los integrantes de la Red de Comités de Participación Ciudadana de los Sistemas Nacional y Locales Anticorrupción, externamos nuestra preocupación por la reforma aprobada el 29 de julio por las Cámaras de Diputados y Senadores con el fin de excluir de la aplicación de la Ley de Adquisiciones, Arrendamientos y Servicios (Ley), a las contrataciones del sector salud que se realicen con la intermediación de organismos internacionales.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Con la reforma, el gobierno federal queda habilitado para contratar mediante un intermediario único (organismo internacional), los medicamentos, insumos, equipo y servicios médicos en el extranjero, sin los mecanismos de competencia (licitación) ni control en cuanto a calidad, precio, condiciones de entrega y evaluación previstos en la Ley. Para tales contrataciones, el Ejecutivo no requerirá tampoco una investigación de mercado para justificar que esta adjudicación es la mejor opción para el Estado.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          En nuestra calidad ciudadana con el encargo de coadyuvar con las autoridades en el combate a la corrupción en el país, queremos señalar que la facultad prevista en la reforma es altamente discrecional y no tiene límite alguno pues deja abierto que pueda hacerse este tipo de contrataciones en cualquier momento aún sin que exista emergencia sanitaria.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Consideramos que la exclusión de todo el régimen de contrataciones públicas a estas compras y servicios de salud impacta negativamente en la transparencia de la propia contratación y hay una relación inversamente proporcional entre transparencia y corrupción pues a menor transparencia mayor corrupción. La opacidad en las adjudicaciones que se realicen bajo este mecanismo también impacta en la disminución de competidores y también hay una relación inversamente proporcional entre competencia y corrupción: mientras más competidores puedan acceder a los mecanismos de contrataciones públicas, menor corrupción se genera. Mecanismos cerrados a la competencia (adjudicación directa o invitación restringida) tienen un mayor riesgo de corrupción.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Los requisitos de las contrataciones públicas previstos en el artículo 9 de la Convención de Naciones Unidas Contra la Corrupción y los Tratados comerciales de los que México es parte (capítulos de compras públicas y anticorrupción), así como el artículo 134 de nuestra Constitución, prevén como regla general el mecanismo de contratación basado en una competencia libre, abierta, transparente, imparcial, con criterios objetivos en la decisión de la adjudicación, que promueva un trato justo y equitativo a todos los proveedores potenciales, así como licitaciones que garanticen al estado las mejores condiciones de precio, calidad, financiamiento y oportunidad. Lo anterior, es opuesto a la reforma aprobada y hacemos notar que, la ciudadanía no podrá evaluar dichas contrataciones públicas pues no les aplica ninguno de los mecanismos de control, desde la planeación hasta la evaluación, previstos en la ley.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Tampoco tendrá la ciudadanía oportunidad de valorar, mediante una investigación de mercado realizada por la dependencia o entidad contratante, que la opción seleccionada a través del intermediario internacional, era la mejor opción disponible en comparación con las demás evaluadas. Lo anterior, merma la certeza ciudadana y nos imposibilita la evaluación de la seguridad y calidad de los insumos o servicios adquiridos.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Confiamos en que las y los legisladores que se opusieron a esta reforma, así como la Comisión Federal de Competencia Económica (COFECE) y el Instituto Nacional de Acceso a la Información y Protección de Datos Personales (INAI) presenten los recursos legales conducentes, para que el Poder Judicial enderece para la ciudadanía, la transparencia y competencia en las adjudicaciones de estas contrataciones públicas.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Red Nacional de Comités de Participación Ciudadana del Sistema Nacional Anticorrupción
        </p>
