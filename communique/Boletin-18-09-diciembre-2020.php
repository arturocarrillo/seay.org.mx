        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Rumbo para el combate a la corrupción<br>Destacan papel de la sociedad en Política Anticorrupción
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Mérida, Yucatán, a 9 de diciembre de 2020.- La propuesta de la Política Estatal Anticorrupción de Yucatán (PEAY) marca el rumbo para un combate más efectivo a la corrupción, la impunidad y la arbitrariedad, señaló la presidenta del Comité de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción de Yucatán (SEAY), Mónica Febles Álvarez-Icaza.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Durante la sesión Solemne del Comité Coordinador del SEAY, realizada en el marco del Día Internacional contra la Corrupción, la presidenta del CPC recordó que la única manera de salir adelante es reconocer que existe una corresponsabilidad social que todos debemos asumir.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Ante los integrantes del Comité Coordinador del SEAY, celebró la confianza que la sociedad depositó en la elaboración de la propuesta, pues dijo cada idea, sugerencia y reclamo se transformaron en una sola voz.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            La propuesta de la PEAY fue entregada al Comité Coordinador del SEAY, integrado por la secretaria de la Contraloría General del Estado de Yucatán (Secogey), Lizbeth Basto Avilés; el magistrado presidente del Tribunal Superior de Justicia de Yucatán y del Consejo de la Judicatura, Ricardo Ávila Heredia; y el magistrado presidente del Tribunal de Justicia Administrativa de Yucatán, Miguel Diego Barbosa Lara.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Asimismo, el comisionado presidente del Instituto Estatal de Transparencia, Acceso a la Información Pública y Protección de Datos Personales (INAIP), Aldrin Briceño Conrado; así como los titulares de la Fiscalía Especializada en Combate a la Corrupción, José Enrique Goff Ailloud, y de la Auditoría Superior del Estado de Yucatán, Mario Can Marín.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Ante el secretario ejecutivo del SEAY, Edwin Rejón Pacheco, Mónica Febles enfatizó que la PEAY, que está dirigida tanto al gobierno como a la sociedad, representa un rumbo para medir el control y combate a la corrupción, en sintonía con la Política Nacional Anticorrupción y con los propios ejes del SEAY, que complementan de manera significativa la propuesta.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            “En la PEAY está esa voz y reclamo ciudadano; está la investigación seria y profunda de fuentes confiables; el trabajo de especialistas para darle forma medible a lo que se pretende alcanzar en este camino; está la norma jurídica para que esa vox populi ciudadana quede capturada en un documento que sea la política rectora del estado de Yucatán para atender de frente la derrota de la corrupción, la legalidad y la integridad”, subrayó.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            En el documento entregado, se establece que la PEAY está integrada por seis ejes, los cuatro primeros van de la mano con la política nacional, y son: Combatir la corrupción e impunidad; combatir la arbitrariedad y el abuso de poder; promover la mejora de la gestión pública y de los puntos de contacto gobierno-sociedad y finalmente, involucrar a la sociedad y el sector privado.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            En tanto que, de manera local, se le añadieron dos ejes más: promover la educación y cultura anticorrupción y fortalecer a los municipios en el combate a la corrupción. Derivados de esos seis ejes, hay 60 objetivos para su implementación.
        </p>
