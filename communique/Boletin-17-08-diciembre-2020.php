        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Libro explica funcionamiento de los sistemas anticorrupción<br>Presentan obra de Óscar Leonardo Ríos García
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Mérida, Yucatán, a 8 de diciembre de 2020.- Mostrar de manera más sencilla y entendible cómo está integrado el Sistema Nacional Anticorrupción (SNA) y sus homólogos en cada estado, así como las leyes e instituciones que lo hacen funcionar, es el punto esencial del libro “Régimen de Responsabilidades de Servidores Públicos a la luz del Sistema Nacional Anticorrupción”, de Óscar Leonardo Ríos García.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Durante la presentación realizada en el marco de la Semana Anticorrupción 2020, el autor señaló que este material resulta oportuno y de interés general pues explica, con términos accesibles, todo el sistema de combate la corrupción para quienes quieren o tienen la intención de coadyuvar a erradicar este flagelo social.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            “Con esta obra quiero que sepan y comprendan cuáles son las herramientas e instrumentos que nos da el Derecho para combatir la corrupción, como estas actividades que presenta el SEAY, son acciones para combatirla y facilitar el entendimiento de algo que parece complejo”, comentó Ríos García.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Asimismo, puntualizó que en su libro se aborda el SNA desde su creación, cuál es su estructura orgánica, además, el régimen de responsabilidades de los servidores públicos, los tipos de faltas que pueden cometer y sus posibles sanciones, así como la responsabilidad política, cómo y quienes pueden ser sujetos a un juicio político.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Al hacer uso de la palabra, la presidenta del Comité de Participación Ciudadana (CPC) del SNA, Rosa María Cruz Lesbros, coincidió en que el material ayudará a comprender de manera más clara el engranaje que se activa para el combate a la corrupción, la garantía de transparencia y la fiscalización de recursos públicos, aunque reconoció, le hubiera gustado se incluyeran más temas igual de importantes.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          “Considero que este libro nos ayudará demasiado para resolver dudas con las que se enfrenta la gente y nos preguntan de manera constante”, afirmó Cruz Lesbros.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Por su parte, la presidenta del CPC del Sistema Estatal Anticorrupción de Yucatán (SEAY), Mónica Febles Álvarez-Icaza, consideró que esta será una lectura obligada pues ayudará no solo a combatir el desconocimiento que tiene la ciudadanía sobre el SNA, sino dejará en claro a los funcionarios y servidores públicos cuáles son sus responsabilidades y hasta dónde pueden llegar sus acciones.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          “Este es un libro debería ser una lectura obligatoria para todo funcionario, servidor público, para sus equipos de trabajo pues les ayudará a mejorar su desempeño y al ciudadano le resolverá dudas”, finalizó.
        </p>
