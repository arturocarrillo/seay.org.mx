        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Compromiso en combate a la ilegalidad <br>Inicia Semana Anticorrupción 2020.
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Mérida, Yucatán, a 7 de diciembre de 2020.- Durante el programa de actividades inaugurales de la Semana Estatal Anticorrupción 2020, la presidenta del Comité de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción (SEAY) Mtra. Mónica Febles Álvarez-Icaza suscribió un convenio de colaboración con la Universidad Anáhuac-Mayab y presenció la conferencia magistral de la Presidenta de Mexicanos contra la Corrupción, Dra. María Amparo Casar Pérez.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            En su discurso de inauguración, Febles Álvarez-Icaza hizo un llamado a la ciudadanía para que se involucre de manera activa en la lucha contra la corrupción, ya que solo así se puede lograr un cambio social para la erradicación de este flagelo.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            “Sabemos que hay situaciones que están preocupando a la ciudadanía, los escuchamos, leemos y estamos trabajando en ello, pero esto no va a rendir frutos sin tu compromiso y tú participación”, sostuvo.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            La presidenta del CPC detalló que a la fecha el SEAY ha emitido ocho recomendaciones públicas para entes obligados, dentro de los cuales se incluye una al Gobierno del Estado sobre la urgencia de presentar una iniciativa de reforma a la Ley de Adquisiciones, que ya tiene más de 30 años.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Puntualizó que se han dado pasos importantes, toda vez que también se aprobó el Mecanismo de Queja, a través del cual se han atendido 59 quejas y orientado a más de 250 ciudadanos.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Además, han firmado 28 convenios de colaboración con cámaras empresariales, colegios de profesionistas, instituciones de educación superior y organismos gubernamentales.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Posterior al acto protocolario, durante la conferencia “Principales estrategias para el combate a la corrupción”, la politóloga e investigadora María Amparo Casar Pérez puntualizó que la corrupción debe combatirse porque afecta directamente a la población más vulnerable del país.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            “La corrupción es un impuesto regresivo, hay que combatirla porque si queremos que sean primero los pobres, está claro que los más afectados son ellos, el 30 por ciento de los hogares con ingresos mensuales menores a los seis mil pesos, son quienes padecen más este impuesto regresivo que es la corrupción”, precisó.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            La también presidenta de Mexicanos contra la Corrupción y la Impunidad, destacó que para tener una verdadera política anticorrupción en México, se debe comprender todo el proceso de ilegalidad, que abarca la prevención, cultura de la denuncia, mecanismos efectivos de detección, investigación de los delitos, sanción, reparación del daño y cierre.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Asimismo, indicó, abonaría el terminar con el uso de dinero en efectivo en las instancias de gobierno y promover la bancarización, respetar a los sistemas nacionales, crear un padrón de beneficiarios y frenar el recorte presupuestal a las instancias anticorrupción y de combate a la impunidad.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            “Son pequeñas medidas que están al alcance de todos, tienen efectos inmediatos y que creo deben ser adoptadas por este gobierno y sobre todo reforzar los mecanismos de transparencia y gobierno; tenemos una hoja de ruta de 10 pasos que no cuesta gran esfuerzo democrático, con esto mejoraríamos tanto el combate a la corrupción”, dijo.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Finalmente, acompañada por el Secretario Ejecutivo del SEAY, Lic. Edwin Rejón Pacheco, la presidenta del CPC suscribió un convenio de colaboración con la Universidad Anáhuac-Mayab.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            El instrumento jurídico tiene el objetivo de fortalecer la cultura anticorrupción entre la comunidad universitaria mediante diversos programas y actividades.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            El rector de esa Institución de Educación Superior, Miguel Pérez Gómez, resaltó la importancia de establecer mecanismos de colaboración entre ambas instancias para poder formar alumnos, bajo el entendimiento de que el realizar un acto de corrupción afecta de manera directa a la población.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            En la firma de este convenio también participaron el Vicerrector Académico de la Universidad Anáhuac-Mayab, Narciso Acuña González; el Vicerrector de Administración y Finanzas, Narcedalia Garza Luna; y el director de la Facultad de Derecho, Absalón Álvarez Escalante.
        </p>
