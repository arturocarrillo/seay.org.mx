        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Ejemplos exitosos de contraloría social<br>Continúan actividades a favor de la cultura anticorrupción
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Mérida, Yucatán, a 8 de diciembre de 2020.- Pasar del señalamiento a la actuación asumiendo el rol de vigilante de los recursos públicos y cómo se ejercen en sus comunidades, es como los ciudadanos ayudarán a erradicar la corrupción en todos los niveles de gobierno, aseguró la presidenta del Comité de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción (SEAY), Mónica Febles Álvarez Icaza.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Al participar en el conversatorio “Contraloría Social y municipios: experiencias ciudadanas en el combate a la corrupción”, que forma parte de las actividades de la Semana Estatal Anticorrupción 2020, la presidenta del CPC reconoció los trabajos de los cuatro integrantes de la Contraloría Social del Ayuntamiento de Progreso, pues, dijo, se han convertido en uno de los tantos casos de éxito que existen ya en el Estado.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          “Hablar de contralorías sociales es hablar de participación ciudadana, está demostrado que una participación proactiva da buenos resultados; nadie conoce mejores las necesidades que los propios habitantes y que ellos vigilen y validen que se usen los recursos de manera eficiente, permitirá lograr los objetivos de combate a la corrupción y transparencia”, aseveró.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Asimismo, la secretaría de la Contraloría General del Estado (SECOGEY), Lizbeth Basto Avilés, precisó que esto es un ejemplo de participación activa de la ciudadanía y de la correcta vigilancia y fiscalización de recursos públicos en los proyectos de mejoramiento urbano, y reiteró la disposición de la dependencia que encabeza para continuar este tipo de acciones.
          </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          En este conversatorio, el director del Catastro y Zona Federal Marítimo Terrestre del Ayuntamiento de Progreso, Ali Yamil Dib Muñoz, detalló que para lograr un desarrollo efectivo de las siete obras que se realizan en distintos puntos del municipio, entre ellos Chelem, se buscaba la participación de la ciudadanía, por lo que emitieron la convocatoria para formar la Contraloría Social para vigilar la ejecución de obras.
          </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          “La gente de Chelem tomó con mucha seriedad el aviso y se acercó; mostraron interés y se han involucrado en cada paso de las dos obras que se realizan con recursos federales en esta comisaría”, destacó.
          </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Durante su intervención, Roger Marín Ruiz, integrante del Comité de Contraloría Social, reconoció que este ejercicio les ha permitido hacer llegar a las autoridades el sentir del pueblo, que conozcan sus necesidades y lo que opinan de las obras, revisar la calidad del material, que realmente sirva para que en un futuro puedan disfrutar de los espacios.
          </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Antes de concluir, el director de Programas Federales de la SECOGEY, Roberto Santiago Magaña González, puntualizó que existe el compromiso para capacitar a los comités de contraloría social y darles el conocimiento de cómo vigilar que los recursos se ejerzan de manera correcta.
          </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          En el marco de esta actividad, se presentó a los tres ganadores del Concurso Estatal de Transparencia en Corto: Aneli Peniche Méndez, con el video “Con el Paso del Tiempo; Esteban Uscanga Huerta con “Transparencia > Corrupción”; y Gabriel Mercadillo Ruiz con su video “La corrupción empieza en casa.
          </p>
