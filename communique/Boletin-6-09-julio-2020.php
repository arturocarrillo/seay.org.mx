        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">Transparencia proactiva apoya participación social<br>Urgen fortalecer mecanismos para rendición de cuentas</h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Mérida, Yucatán, a 09 de julio de 2020.- El Comité de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción de Yucatán (SEAY) hizo un llamado para que las autoridades fortalezcan los mecanismos de intercambio de información y transparencia proactiva, con el objetivo de reducir el riesgo de que se cometan actos de corrupción durante la reactivación de los sectores económicos y sociales que actualmente se lleva a cabo, en el marco de la emergencia sanitaria por Covid-19.  
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          La reciente aprobación de las Políticas de Transparencia Proactiva del Instituto Estatal de Transparencia, Acceso a la Información Pública y Protección de Datos Personales (Inaip Yucatán), constituyó un avance que permitirá a los ciudadanos contar con herramientas útiles para analizar las acciones del gobierno y, sobre todo, darle seguimiento al destino de los recursos y la eficiencia de los programas extraordinarios implementados en los últimos meses.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Como parte de las actividades sustantivas que realiza el CPC del SEAY, se establecieron una serie de consideraciones para darle trazabilidad a los recursos documentando las acciones que se lleven a cabo durante la emergencia sanitaria.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          En ese sentido, cabe destacar que el pasado 17 de abril la Secretaría de la Contraloría General del Estado (Secogey), que forma parte del SEAY, publicó el Acuerdo SCG 12/2020 por el que se expiden los lineamientos para la elaboración e integración de libros blancos de los programas derivados del Plan Estatal para impulsar la Economía, los Empleos y la Salud.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         Dichos lineamientos tienen como finalidad que las dependencias y entidades elaboren un libro blanco por cada uno de los programas que les corresponda ejecutar con motivo del plan estatal, para dejar constancia de las acciones y resultados obtenidos de estos, así como para documentar los resultados alcanzados.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Sin embargo, se prevé que la integración de esos instrumentos deberá concluirse a más tardar el último día hábil de 2020, lo que permitirá comprender, hasta entonces, el detalle de cómo operaron las dependencias estatales durante la actual emergencia sanitaria. 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Por ello, el CPC del SEAY, que preside Mónica Febles Álvarez-Icaza, reiteró que es imprescindible que los responsables del gasto público proporcionen información necesaria para conocer la trazabilidad, el origen y el destino de los recursos que se tienen disponibles, así como las afectaciones presupuestarias que tendrán en el erario durante este periodo extraordinario.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Bajo estos enfoques, se enfatiza que la transparencia y la integridad deben regir en todo momento, pero especialmente deben hacerlo en momentos de crisis, en donde lo primordial es preservar los derechos humanos de la población, a través del especial cuidado en el registro y comprobación del ejercicio del gasto destinado a apoyos extraordinarios, concluyó el CPC del SEAY.
        </p>
