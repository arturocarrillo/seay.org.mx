        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">Piden investigación a servidores públicos<br>Presentan queja por solicitudes de apoyos al desempleo</h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">Mérida, Yucatán, a 30 de abril de 2020.- La Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SEAY) activó su Mecanismo de Queja, por lo que iniciarán los procesos de investigación contra servidores públicos que intentaron aprovecharse de los recursos que se otorgan debido a la epidemia de Covid-19, en particular los destinados al Programa de Desempleo.
       </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">La presidenta del Comité de Participación Ciudadana (CPC) del SEAY, Mónica Febles Álvarez-Icaza, suscribió la respectiva queja debido a las posibles conductas de servidores públicos que pretendieron acceder a esos apoyos para desempleados, lo que revela que podrían existir inequidad, irregularidades e, incluso, faltas administrativas por parte de aquellos que los solicitaron. 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">“La ciudadanía ya no tolera la falta de ética con la que debe conducirse cualquier servidor público, particularmente en casos como este”, expresó Febles Álvarez-Icaza.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">Con motivo de la emergencia sanitaria por el Covid-19, el Gobierno del Estado dispuso de acciones extraordinarias para hacerle frente a las crisis de salud y económica, entre las que destaca el <span>Programa de Desempleo.</span></p>

        <p class="text-md text-green-s1 font-weight-normal p-4">Tras la publicación del listado de beneficiarios, hubo señalamientos en medios y redes sociales sobre una injusticia por la inequidad en la que algunos servidores públicos incurrieron cuando presentaron solicitudes para pretender acceder, indebidamente, a esos recursos.</p>

        <p class="text-md text-green-s1 font-weight-normal p-4">Es así como a través del Mecanismo de Queja del SEAY, la presidenta del CPC promovió que se inicien las acciones correspondientes para que se pongan en conocimiento del Comité Coordinador del propio Sistema posibles hechos de corrupción o faltas administrativas.</p>
        
        <p class="text-md text-green-s1 font-weight-normal p-4">De ese modo, también solicitó dar vista a las instancias del Gobierno del Estado, organismos autónomos y ayuntamientos, para que en el ámbito de sus facultades inicien las investigaciones correspondientes.</p>

        <p class="text-md text-green-s1 font-weight-normal p-4">Asimismo, pidió que además “pueda ser valorada esta acción reprochable sobre la base del Código de Ética con que se deben conducir los servidores públicos”.</p>

        <p class="text-md text-green-s1 font-weight-normal p-4">“Estamos convencidos que una participación ciudadana activa, nos podrá ayudar como sociedad a enfrentar el flagelo de la corrupción e impunidad”, concluyó la presidenta del CPC del SEAY.</p>

