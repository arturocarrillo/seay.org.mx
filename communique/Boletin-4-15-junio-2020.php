        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">Entrega de apoyos bajo monitoreo<br>Investigación revela carencias de información en municipios</h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
         Mérida, Yucatán, a 15 de junio del 2020.- Resultado del monitoreo efectuado por la presidencia ciudadana del Sistema Estatal Anticorrupción de Yucatán (SEAY), fueron detectados seis ayuntamientos que carecen de estrategias digitales para informar a la ciudadanía sobre el destino y aplicación de los recursos liberados con motivo de la emergencia sanitaria por la Covid-19.  
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         Mediante una investigación realizada por la Secretaría Ejecutiva del SEAY, se encontró con los ayuntamientos de Cuzamá, Chankom, Halachó, Sotuta, Tahdziú y Yobaín, son los únicos de los 106 municipios yucatecos que no cuentan con página web ni han implementado algún otro medio de consulta durante los meses de abril y mayo, cuando el Gobierno del Estado puso en marcha programas de apoyo por la epidemia.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
        El informe, hecho a solicitud del Comité de Participación Ciudadana del SEAY, detalla que 87 municipios más han realizado publicaciones de los apoyos recibidos en sus redes sociales, particularmente en páginas de Facebook.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
        Es así como el reporte también indica que el 92 por ciento de los ayuntamientos utilizan redes sociales para difundir las actividades que realizan específicamente en esta emergencia sanitaria.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         Por otra parte, los ayuntamientos de Homún, Mérida, Tahmek, Tekax y Valladolid publicaron en sus respectivas gacetas municipales las declaratorias de emergencia y los programas que se pondrían en marcha para hacerle frente a este problema de salud pública.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         Con relación a la utilización de un micrositio en el cual se detallen los apoyos económicos y medidas de prevención durante la epidemia, solo Mérida cuenta con la información en su sitio de internet y se encuentra en constante actualización.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Este monitoreo fue realizado como parte de los compromisos asumidos por la presidencia ciudadana del SEAY, con el objetivo de compartir con la ciudadanía las acciones difundidas por cada municipio.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         La investigación fue de gabinete y consistió en el seguimiento y recopilación de los informes generados de manera digital por los municipios, de tal manera que los datos fueron obtenidos a través de las páginas web o redes sociales de los ayuntamientos y sus autoridades.
        </p>
