        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Advierten riesgos de corrupción en elecciones<br>Max Kaiser dicta conferencia en el marco de la Semana Anticorrupción 2020
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
             Mérida, Yucatán, a 9 de diciembre de 2020.- Los sistemas estatales anticorrupción deben privilegiar su construcción integrando perfiles que respondan a las demandas de la sociedad, así como trabajar para ganar la confianza ciudadana, advirtió el presidente de la Comisión Anticorrupción de la Confederación Patronal de la República Mexicana (Coparmex), Max Kaiser Aranda.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            En el marco de las actividades de la Semana Anticorrupción 2020, impartió la conferencia “La redefinición del poder en México y la lucha Anticorrupción”, donde enfatizó que la construcción más importante de estas instituciones es con la sociedad, para que sean sus ojos.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Ante Mónica Febles Álvarez-Icaza, presidenta de del Consejo de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción de Yucatán (SEAY), Kaiser Aranda detalló que los próximos retos estarán al proceso electoral que se desarrollará en 2021, el cual se suma a la llegada de una vacuna contra la Covid-19 que todos los gobiernos y ciudadanos querrán adquirir.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            “Por eso la Organización Nacional de las Naciones Unidas está preocupada y habla de la recuperación con integridad el Día Internacional de la Lucha contra la Corrupción este 2020, porque sabe los riesgos de corrupción que genera la pandemia, son siete billones de personas las que quieren un mismo producto, la vacuna, esto generará una presión brutal para los gobiernos de los tres rubros”, comentó.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Por lo anterior, precisó, tanto el Sistema Nacional Anticorrupción, como los organismos estatales deberán ser vigilantes de que las vacunas se compren mediante procesos transparentes y legales, que se distribuyan de manera correcta y que se le dé a quienes más los necesitan.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            En el caso de las elecciones, el presidente de la Comisión Anticorrupción de la Coparmex explicó que la corrupción comprende cuatro elementos, iniciando por los comicios, ya que hay estudios donde indican que nueve de cada 10 pesos que se gastan en campañas no son reportados a las autoridades, recursos que son obtenidos de tres fuentes: empresarios, exservidores públicos y el crimen organizado.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            A esto, le siguen las designaciones, es decir, los inversionistas colocan personas de su confianza para que vigilen cómo se maneja el recurso; el tercer punto son las contrataciones públicas, dijo, ahí se paga la inversión hecha en el candidato asignando contratos de servicio y obra de manera directa.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            El último eslabón, puntualizó, se trata de la ejecución de los contratos donde se recupera el dinero ocupando menos materiales o de precio más barato, menos trabajadores, o expandiendo el tiempo de elaboración.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Aunque Kaiser Aranda reconoció que no hay una solución concreta para eliminar la corrupción, señaló que se puede evitar erradicando el uso de dinero en efectivo en las campañas y reduciéndose a 10 días. 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            A esto le suma tener perfiles preestablecidos, mecanismos competitivos y sanciones para quien designe alguien que no se merece el cargo, así como generar una Ley General de Contrataciones Públicas, pues existen 66 distintos regímenes para este proceso.
        </p>
