        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Dibujo, cómics y héroes por la integridad<br>Concluye la Semana Anticorrupción 2020
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Mérida, Yucatán, a 11 de diciembre de 2020.- Con la premiación de los seis ganadores del concurso Infantil de Dibujo y Cómic, así como la entrega de medallas a cuatro Héroes Anticorrupción, concluyeron las actividades de la Semana Estatal Anticorrupción 2020.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Un total de 40 niñas y niños respondieron a la convocatoria emitida por el Comité de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción (SEAY), de tal manera que se registraron 24 obras en la categoría de dibujo y 16 en la de cómic; del total de trabajos, 12 correspondían a alumnos de educación indígena.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Para poder participar, enviaron sus ilustraciones representando acciones que promovían la cultura de la integridad y el combate a la corrupción, con lo que se incentivó desde la infancia una cultura que promueva la ética en todo lo cotidiano.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          En la ceremonia de premiación, transmitida por internet, se anunció que María José Balaam Mas obtuvo el primer lugar en la categoría de dibujo, mientras que Diana Berenice Noh Moo y Carlos Rubio Maglioni, se llevaron el segundo y tercer lugar, respectivamente.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Para la categoría de cómic, ganaron Naomi Pérez González con el primer lugar, Miguel Navarrete Aguiar en el segundo, y Nolte Alarich en el tercer lugar.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Los premios consistieron en una Tablet para el primer lugar; el segundo puesto un kit de colores profesionales, y el tercero una mochila.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          En el mismo evento, recibieron un reconocimiento como Héroes Anticorrupción: Enna Margarita Ricalde Ricalde, Irma Mexicano Hernández, Reyes Manuel Castro García y Martín Díaz Martínez, quienes fueron nominados por familiares o amigos por ser consideradas personas que se conducen con ética y responsabilidad.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Tras la presentación de los trabajos, la presidenta del Comité de CPC del SEAY, Mónica Febles Álvarez-Icaza felicitó a todos los galardonados por mostrar su compromiso por tener un mejor país.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          “Todos los sectores de la sociedad deben poner de su parte e involucrarse. Con esto damos por concluida nuestra semana anticorrupción pero mañana, pasado y todos los días debemos seguir trabajando por esto”, dijo.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Estuvieron presentes los también integrantes del CPC Javier Montes de Oca Zentella, Manuel Jesús Castillo Rendón, José Luis Villamil Urzaiz e Ivana Ongay Xacur, así como el titular de la Secretaría Ejecutiva del SEAY, Edwin Rejón Pacheco.
        </p>
