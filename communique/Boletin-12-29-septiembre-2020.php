        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Ciudadanía es fundamental contra la corrupción<br>Dan a conocer la propuesta de Política Estatal Anticorrupción de Yucatán
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Mérida, Yucatán, a 29 de septiembre de 2020.- La Política Estatal Anticorrupción de Yucatán (PEAY) está diseñada para que a largo plazo y sin importar los cambios de gobiernos, sea una herramienta eficiente en el combate a la corrupción al ser medible y evaluable, sostuvo la presidenta del Comité de Participación Ciudadana (CPC) del SEAY, Mónica Febles Álvarez-Icaza.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Con el objetivo de socializar la propuesta de la PEAY, se publicó el documento en el cual se encuentra el diagnóstico del problema a nivel estatal y los alcances de la política, así como los detalles sobre los ejes estratégicos y las bases para su implementación.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Para su elaboración, explicó Febles Álvarez-Icaza, se llevaron a cabo consultas directas con la ciudadanía, municipios y un Consejo Técnico, además de que fueron consultadas fuentes nacionales e internacionales sobre corrupción.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Recordó que a nivel nacional existe ya la Política Nacional Anticorrupción (PNA), por lo que la PEAY buscó alinearse con ésta a fin de que ambas trabajen en la misma dirección.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Como resultado de los procesos de consulta, se identificaron factores, causas y efectos de la corrupción que van desde la existencia de altos niveles de impunidad; situaciones que propician la arbitrariedad y el abuso del poder; así como un contacto entre gobierno y sociedad distorsionados por actos de corrupción.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Agregó que también fueron señaladas una débil participación de la sociedad y el sector privado en el control de la corrupción; una escasa labor para socializar valores de integridad que aporten a la cultura anticorrupción; y, finalmente, limitadas acciones de combate a la corrupción en los municipios.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          A partir de ese panorama, la también presidenta del Sistema Estatal Anticorrupción de Yucatán (SEAY) dijo que se fijaron 60 prioridades, mismas que fueron agrupadas en seis ejes estratégicos.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          De ese modo, detalló, la PEAY plantea sus cuatro primeros ejes totalmente alineados con la política nacional: Combatir la corrupción e impunidad; combatir la arbitrariedad y el abuso de poder; promover la mejora de la gestión pública y de los puntos de contacto gobierno-sociedad; e involucrar a la sociedad y el sector privado.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Para el caso de Yucatán, Mónica Febles precisó que se agregaron dos ejes más, los cuales prevén promover la educación y cultura anticorrupción; y fortalecer a los municipios en el combate a la corrupción.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         Por su parte, la Secretaría Ejecutiva del SEAY anunció que el documento completo de la PEAY, así como un resumen ejecutivo, se encuentran disponibles en el sitio www.seay.org.mx
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         En el mismo sitio de internet también se puede participar en la encuesta ciudadana para evaluar la propuesta de la PEAY.
        </p>
