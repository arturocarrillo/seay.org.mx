    <li>Formas de sesionar, periodos, cantidad mínima de sus integrantes para avalar sus decisiones. </li>
    <li>Propiciar la emisión, aplicación y vigilancia del código de ética y de conducta, en caso de no existir.</li>
    <li>Revisar y actualizar periódicamente la normatividad establecida en el código de ética y de conducta.</li>
    <li>Promover buenas practicas entre la comunidad estudiantil a fin de destacar los valores del alumnado.</li>
    <li>Fomentar la cultura ética e integridad dentro de la institución educativa.</li>
  </ol>
