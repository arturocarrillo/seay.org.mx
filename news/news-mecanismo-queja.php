				<h2 class="title text-blue_1">Presentación del Mecanismo de Queja
			</div>
			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
					 Martes 2 de julio de 2019 , los integrantes del Comité de Participación Ciudadana del Sistema Estatal Anticorrupción de Yucatán presentaron en rueda de prensa el “Mecanismo de Queja ” el cual tiene como finalidad dar atención y trámite a las quejas presentadas por parte de la ciudadanía ante la Comisión Ejecutiva o la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán, por actos u omisiones de autoridades o servidores públicos, o particulares por la presunta comisión de faltas administrativas o hechos de corrupción, a efecto de emitir, en caso de ser procedente, una recomendación pública de carácter institucional que contenga una política pública, con propósito de fortalecer la cultura de integridad y el comportamiento ético.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					 La Presidenta del Sistema Estatal Anticorrupción, M. en D. Graciela A. Torres Garma informó que el mecanismo de queja está pensado para que sea lo más accesible para la ciudadanía, es por ello que el trámite de la queja no tiene costo y pueden ser presentada de manera presencial en las oficinas de la Secretaria Ejecutiva del Sistema, o a través del apartado de mecanismo de queja en el portal web www.seay.org.mx y vía correo electrónico a la dirección quejas@seay.org.mx, también se prevé que puedan ser presentadas vía oral cuando el ciudadano no pueda escribir o sea menor siempre que esté presente su tutor o representante legal.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Al cuestionarse por parte de los medios la falta de carácter sancionador del mecanismo de queja, la Presidenta del Sistema aclaró que aun cuando la recomendación pública que se pueda emitir no tiene como finalidad sancionar, si se dará parte a las autoridades competentes para que en su caso investiguen y sancionen de ser conducente.
				</p>
        <p class="paragraph-md text-blue_1 p-2">
          El Comité de Participación Ciudadana insistió en otorgar el beneficio de la duda al mecanismo de queja y darle la oportunidad para ver su funcionamiento, recordando que se trata de una herramienta novedosa en la cual la participación de los ciudadanos es vital.
            <br>
          En la rueda de prensa estuvieron presentes los integrantes del Comité de Participación Ciudadana (CPC), M. en D. Graciela A. Torres Garma, Presidenta del Sistema Estatal Anticorrupción, Lic. Mónica Febles Álvarez-Icaza, Lic. Javier Montes de Oca Zentella, Arq. Manuel Jesús Castillo Rendón y el Dr. José Luis Villamil Urzaiz, además del Lic. Edwin Manuel Rejón Pacheco, Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción (SESEAY).
        </p>
