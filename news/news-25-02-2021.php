      <div class="w-100 d-flex flex-wrap justify-content-center mt-5 p-2">
        <h2 class="text-md text-green-s1 font-weight-bold m-auto p-4">
          Presentación de la “Agenda colaborativa (gobierno-sociedad) para lograr un mejor combate a la corrupción”.
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
          El jueves 25 de febrero del presente año, en punto de las 18:00 hrs. en modalidad virtual vía ZOOM, se llevó a cabo la presentación de la: “Agenda colaborativa (gobierno-sociedad) para lograr un mejor combate a la corrupción”, el Comité de Participación Ciudadana en colaboración con la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán convocaron al sector empresarial de la entidad. 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          La presidente del Comité de Participación Ciudadana del Sistema Estatal Anticorrupción de Yucatán, la M. en F. Mónica Alicia Febles Álvarez Icaza, se dirigió a todos los presentes dando detalles de los tres importantes temas de la agenda: La participación del Diputado Mario Alejandro Cuevas Mena y el proceso que se está realizando con la Ley de Adquisiciones, la presentación del curso de Integridad Empresarial modalidad online, el cual se puede cursar desde un micrositio de la página web de la SESEAY y la inauguración del observatorio electoral.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          La sesión tuvo la presencia del Diputado Mario Alejandro Cuevas Mena, como invitado especial, el Lic. Alejandro Rivas (CIRT), Lic. Roberto Carlos Castro Carrillo (ASOFOM), Lic. Felipe Solís Mier y Terán (CANACO), Lic. Namen Charruf (CANACINTRA), Lic. Rolando Mendoza Hijuelos (COPARMEX), Lic. Fernando Ponce Díaz (COPARMEX), Lic. Gicelly Patrón (CETUR), Lic. Addy Sánchez Rivera (AAPROTUY), Lic. Juan José Martín Pacheco (Asociación de Hoteles en Yucatán).
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          En primera instancia, la Presidenta del CPC del SEAY, Lic. Febles, presentó al Dip. Alejandro Cuevas el cual explicó el proceso que está en curso sobre la Ley de adquisiciones y respondió a las dudas del Lic. Mendoza de la COPARMEX y del Lic. Solís de la CANACO; explicó que se llavará a cabo la presentación bajo la modalidad de Parlamento abierto y enfatizó su disposición para trabajar demanera conjunta y reconoció el trabajo que ha realizado el CPC en relación con este tema.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          El Diputado Mario Alejandro Cuevas Mena, invita a las Cámaras Empresariales y al Sistema Estatal Anticorrupción Yucatán, a contactarle para poder obtener más información sobre la reforma a la Ley de Adquisiciones, al igual que, en caso de tener una propuesta de reforma o petición, él pueda servir de vínculo para que estas inquietudes sean atendidas.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Posteriormente, la propia presidenta del CPC, presentó el curso en línea que pueden llevar a cabo todas las organizaciones de nuestra entidad sobre la “Integridad Empresarial”, habló sobre los riesgos que tienen las empresas que no están al corriente de esta obligación empresarial. Por su parte, la Dra. Ivanna Ongay, integrante del CPC, presentó a detalle los contenidos del curso que ya están disponibles en la página web del SESEAY, exhortó a los presentes a inscribirse al curso, el cual es completamente gratis, y subrayó la importancia de cumplir con esta obligación.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          El Sistema Estatal Anticorrupción de Yucatán, se comprometió a servir de herramienta para las Cámaras empresariales en materia de capacitación y actualización, referente a la Ley de Responsabilidades Administrativas.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          De igual manera, el Sistema ofrece de manera gratuita curso online Compliance Empresarial que se encuentra disponible en la página del Sistema:  www.seay.org.mx
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Por último, la Mtra. Febles explicó el proceso que se llavará a cabo con el observatorio electoral, expuso la importancia que tiene este proceso considerando el contexto en el que nos encontramos, los procesos de transparencia que son necesarios eficientar y la Participación Ciudadana tan necesaria en este año electoral en particular.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         Cabe destacar la presencia de los cinco integrantes del CPC, además de la Mtra. Mónica Febles, se contó con la presencia del Lic. Javier Montes de Oca Zentella, el Arq. Manuel J. Castillo Rendón, Dr. José Luis Villamil Urzaíz y la Dra. Ivanna Ongay Xacur. Además estuvo presente el Secretario Técnico de la SESEAY, Lic. Edwin Manuel Rejón Pacheco, la Directora del Departamento de Políticas Públicas, Mtra. Landy Elizabeth Mendoza Seba, directora de Vinculación Interinstitucional, Mtra Laura Espadas Herrera, y personal de la SESEAY.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         La reunión virtual culminó a las 19:00, con el compromiso por parte de los presentes de trabajar colaborativamente, la presidenta del SEAY, Mtra. Febles, agradeció la presencia de los presentes y conminó a considerar la importancia de los temas tratados durante la sesión.
        </p>
