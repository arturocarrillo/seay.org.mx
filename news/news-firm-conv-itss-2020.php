				<h2 class="title text-blue_1">Firma del Convenio con el Instituto Tecnológico Superior del Sur (Oxkutzcab)</h2>
			</div>
			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
            Viernes 10 de enero de 2020, en punto de las 11:30 hrs. en el auditorio del Instituto Tecnológico Superior del Sur, se llevó a cabo la firma de convenio de colaboración entre la Secretaría Ejecutiva del Sistema Estatal Anticorrupción (SESEAY) y el Instituto Tecnológico Superior del Sur (ITSS), la mesa de honor estuvo conformada por el Mtro. José Enrique Carrillo Duarte, Director General del Instituto, el Ing. Roberto Ku Sandoval, Subdirector de Planeación Y Vinculación, la Lic. Claudia Candelaria Vázquez Molina, Subdirectora Académica, y la Dra. Yurbi Alejandra Suárez Alemán, Subdirectora de Administración y Finanzas del Instituto., por parte del Sistema Estatal Anticorrupción la mesa estuvo representada por el Arq. Manuel J. Castillo Rendón, integrante del Comité de Participación Ciudadana y la Presidenta del Sistema Estatal Anticorrupción la M. en D. Graciela Alejandra Torres Garma.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					  En primera instancia, tomó la palabra el Director General del ITSS, el Mtro. Carrillo Duarte, el cual hizo referencia a la importancia de celebrar compromisos en los que estén representados los valores institucionales, hizo hincapié en la formación integral de los estudiantes y en la importancia de fomentar valores que se vean representados en el bien común, habló sobre la importancia de la creación de instancias como la SESEAY, por su parte la M. en D. Torres Garma, presidenta del Sistema Estatal Anticorrupción enfatizó la importancia de crear en los estudiantes una actitud reflexiva orientada al bienestar social, mencionó los alcances del convenio de colaboración en donde los alumnos tendrán la oportunidad de vincularse directamente con el Sistema Estatal Anticorrupción y tener una acción directa en pro de la lucha contra la corrupción.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Finalmente se impartió la conferencia: “Hablemos de corrupción” impartida por el Mtro. Luis Guillermo Juárez Ramírez, jefe del departamento de enlace con autoridades y organizaciones de la SESEAY, en la que tuvo la oportunidad de reflexionar con el personal directivo, administrativo, docentes y alumnos sobre las consecuencias e implicaciones de una conducta corrupta, la importancia de tener una actitud responsable ante tal fenómeno y la importancia de tener una formación integral, con valores y ética en su ejercicio personal y profesional. 
				</p>
