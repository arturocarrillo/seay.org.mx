        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Por su parte, la M. en F. Mónica Febles Álvarez-Icaza, presidenta del Comité de Participación Ciudadana del SEAY, fue la encargada de la presentación del objetivo del Observatorio Electoral: Brindar a las personas candidatas un micrositio para reunir información relevante para la ciudadanía. Las tres instancias involucradas coordinan esfuerzos para que se pueda registrar de manera sencilla los siguientes elementos: La declaración 3 de 3 patrimonial, de interés y fiscal, la declaración 3 de 3 contra la violencia de género, los compromisos anticorrupción, el compromiso de transparencia y el gasto de campaña por partido político, mencionó el objetivo, el contenido y las fechas, así como los informes periódicos y el informe final que se realizará antes del cierre de campañas.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Posteriormente, la Mtra. Gilda Segovia Chab, realizó la presentación de la propuesta de la plataforma y la información que va a contener, la Lic. Lourdes Rosas fue la encargada de canalizar las dudas y/o comentarios de los asistentes. Durante la sesión, se mantuvo una participación activa y de aprobación por parte de los representantes de los diversos partidos políticos del Estado, enfatizando con sus comentarios su compromiso a hacer uso de esta plataforma y la importancia de la misma, ya que permitirá mayor transparencia e información sobre los candidatos, de modo que la ciudadanía tenga más seguridad al ejercer su voto y se promueva una mayor participación en los próximos ejercicios electorales. <br> El evento culminó aproximadamente a las 18:45.
