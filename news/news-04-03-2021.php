        <p class="text-md text-green-s1 font-weight-normal p-2">
          Este jueves 04 de marzo del 2021, en punto de las 18:00 horas, vía ZOOM se llevó a cabo la presentación del Observatorio Electoral “ILEJ” a los partidos políticos de nuestra entidad. Este es un esfuerzo conjunto entre el Instituto Estatal de Acceso a la Información Pública Yucatán (INAIP), el Instituto Electoral y de Participación Ciudadana Yucatán (IEPAC) y el Comité de Participación Ciudadana del Sistema Estatal Anticorrupción Yucatán (CPC).
