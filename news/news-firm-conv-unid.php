				<p class="paragraph-md text-blue_1 p-2">
            En punto de las 09:00 a.m. este viernes 25 de octubre de 2019, en la sala de juicios orales de la Universidad Interamericana para el desarrollo sede Mérida, se llevó a cabo la firma del convenio de colaboración entre la propia institución y la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY).
				</p>
				<p class="paragraph-md text-blue_1 p-2">
