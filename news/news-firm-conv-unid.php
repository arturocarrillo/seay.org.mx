            <li>Lic. Edwin Manuel Rejón Pacheco, Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán.</li>

        </ol>
        <p class="paragraph-md text-blue_1 p-2">
           En primera instancia se otorgó la palabra al Dr. Malo García, el cual enfatizó sobre la importancia de celebrar este tipo de convenios, habló sobre lo necesario de generar en los estudiantes una actitud crítica, reflexiva, basada en valores y en principios, además de brindar herramientas para que los alumnos tengan un desarrollo integral y holístico. Por su parte, la Mtra. Torres Garma agradeció a la institución la apertura para firmar convenios que nos benefician en el futuro inmediato, además de reconocer el desarrollo que ha tenido la UNID en los últimos años, habló de su modelo dual y sus servicios de vanguardia educativa, instó a la institución a darle un seguimiento permanente al convenio y los trabajos que del mismo se deriven en pro del desarrollo del estudiante y por supuesto de la ciudadanía. Acto seguido, se procedió a la firma del convenio. <br>
            Por último, fue el turno del Mtro. Luis Guillermo Juárez Ramírez el cual impartió la conferencia: “Hablemos de corrupción” en la que alumnos, docentes y autoridades presentes analizaron las implicaciones que tiene la corrupción en nuestros días a nivel personal, social y profesional.
