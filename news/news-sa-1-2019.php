      <div class="w-100 mt-5 mb-5 flex">
        <p class="paragraph-md text-blue_1 p-2">
          Martes 03 de diciembre de 2019, en punto de las 11:00 a.m. en las instalaciones del Instituto Nacional de Transparencia Acceso a la Información y Protección de Datos Personales (INAIP)  y en el marco de la Semana Anticorrupción se llevó a cabo la conferencia: “La transparencia como herramienta contra la corrupción” por parte del M en D Aldrín Martín Briceño Conrado , Comisionado Presidente del INAIP. <br>En ésta, el ponente enfatizó la importancia de contar con un proceso administrativo público en la que la rendición de cuentas y la transparencia sean los elementos más importantes que rijan la función pública. Mencionó una serie de datos estadísticos para situar en contexto a la audiencia sobre la situación que vive el país en relación a otros países en cuanto al fenómeno de la corrupción.
          Así mismo, el Mtro. Briceño ejemplificó con tres casos algunas experiencias relacionadas al tema en cuanto a la carencia de transparencia y por último enfatizó sobre posibles acciones para evitar que estos casos se sigan presentado en nuestro país. <br>
          Al término de su ponencia, la Presidente del Sistema Estatal Anticorrupción del Estado de Yucatán, la M. en D. Graciela A. Torres Garma, agradeció al Mtro. Briseño su ponencia, entregó la constancia correspondiente y un obsequio a la vez que dirigió unas palabras al Comisionado Presidente del INAIP.
          Al evento asistieron la Lic. Mónica Febles Álvarez-Icaza, el Arq. Manuel Jesús Castillo Rendón, integrantes del Comité de Participación Ciudadana, la Mtra. Martha Elena Gómez Nechar, Titular de la Unidad de Contraloría Municipal de Mérida, y funcionarios públicos de esta dependencia, además de autoridades y personal administrativo del propio INAIP.
