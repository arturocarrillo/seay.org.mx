				<p class="paragraph-md text-blue_1 p-2">
					 Jueves 14 de junio de 2019, en punto de las 9:30 hrs. en las instalaciones de la Universidad Vizcaya de las Américas, se llevó a cabo la firma del Convenio de colaboración entre la propia institución y el Sistema Estatal Anticorrupción; en breve acto protocolario cedieron la palabra en primera instancia al Dr. José de Jesús Palafox Quintero, rector de la institución, el cual enfatizó la importancia de firmar convenios de colaboración entre ambas instancias y exhortó a los estudiantes de la universidad, en su mayoría estudiantes de la carrera de derecho presentes en la sala de juicios orales de dicha institución, a participar y aprovechar la firma de este tipo de convenios; posteriormente fue el turno del Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción el Lic. Edwin Manuel Rejón Pacheco, el cual agradeció  a la Universidad la apertura para la firma de este tipo de convenios, instó a la participación ciudadana y los jóvenes estudiantes a tener una participación activa y decidida en relación a lo que acontece en el país, así mismo, resaltó las acciones que está llevando a cabo el Comité de Participación Ciudadana y los planes que tiene la Secretaría en el futuro inmediato.  
				</p>
				<p class="paragraph-md text-blue_1 p-2">
          Por último, tomó la palabra la Presidenta del Sistema Estatal Anticorrupción, la lic. Graciela Alejandra Torres Garma, la cual en su discurso enfatizó el compromiso de demostrar que “la corrupción no somos todos, la solución si”. Una vez firmado el documento por las tres máximas autoridades, se impartió una conferencia titulada: “Hablando de corrupción” impartida por el Mtro. Luis Guillermo Juárez Ramírez, Jefe de enlace de la misma SESEAY.
				</p>
        <p class="paragraph-md text-blue_1 p-2">
          A su vez, en el acto protocolario estuvieron presentes el LCC. Javier Montes de Oca Zentella y el Arq. Manuel Castillo Rendón, miembros del Comité de Participación Ciudadana, así como la Lic. Laura Espadas Herrera, Directora de Vinculación Interinstitucional de la SESEAY y la Lic. Patricia Prieto Sánchez, Jefa del Departamento de Control de Indicadores de la SESEAY.
