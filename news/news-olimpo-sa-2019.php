        <h2 class="title text-blue_1">Diálogo "Acciones y Propuestas Aticorrupción". La ciudadanía marca las políticas anticorrupción.</h2>
      </div>
      <div class="w-100 mt-5 mb-5 flex">
        <p class="paragraph-md text-blue_1 p-2">
          Mérida, Yucatán, 6 de diciembre de 2019.- Con un llamado a que todas y todos seamos protagonistas de nuestras comunidades favoreciendo cambios basados en la civilidad, responsabilidad y honestidad, la presidenta Comité de Participación Ciudadana del Sistema Estatal Anticorrupción (SEAY), Graciela Alejandra Torres Garma, abrió la mesa de diálogo <span>“Acciones y propuestas anticorrupción”</span>, en la que participaron diversos actores del servicio público que suman esfuerzos para este objetivo.<br>
          En el acto, inaugurado por el alcalde de Mérida, Renán Barrera Concha y en el que estuvieron presentes los integrantes del Comité Coordinador del SEAY, la presidenta precisó que hay que darles respuestas a una ciudadanía cada vez más exigente y consiente de las repercusiones de las conductas deshonestas.<br>
          En ese tenor, apuntaron que se requiere fomentar la cultura de la legalidad y difundir cuáles son los mecanismos de denuncia a la que pueden recurrir los ciudadanos cuando ocurran ese tipo de hechos. <br>
          “Tenemos que unirnos todos los ciudadanos para que la concordia, honestidad, transparencia y rendición de cuentas sean el común denominador de una administración comprometida con la ciudadanía”, recalcó.<br>
          Torres Garma dijo que el empoderamiento de las acciones ciudadanas darán la pauta que determinarán las políticas públicas de la sociedad, como es el caso de la política anticorrupción “que nació de la ciudadanía para la ciudadanía”.<br>
          En ese sentido, hizo un exhorto a cambiar el paradigma para que la bandera sea la anticorrupción, en la que todos seamos factores de cambio en nuestra comunidad, protagonistas de una verdadera transformación en el que la civilidad, responsabilidad y honestidad sean la carta de presentación.<br> 
          Por su parte, el alcalde Renán Barrera recordó que la corrupción está presente en todas partes y en distintas magnitudes, por lo que las instancias de gobierno tienen que ir mejorando y perfeccionando sus procesos para combatirlo.<br>
          Señaló que actualmente las autoridades están obligadas a transparentar sus programas, planes, presupuestos y acciones, mientras que la ciudadanía tiene que presionar y generar información cuando detecte casos ilícitos. La diferencia, precisó, es que los gobiernos estén dispuestos a corregir el rumbo en vez de ocultar los movimientos de su interior. <br>
          Al arrancar la mesa de diálogo “Acciones y propuestas anticorrupción”, el titular de la Auditoría Superior del Estado (ASEY), Mario Can Marín, puso énfasis en el trabajo que realizan con los ayuntamientos yucatecos para instalar sus contralorías, lo que servirá para subsanar observaciones durante el proceso de fiscalización. <br>
          Mientras tanto, el vicefiscal Anticorrupción, Enrique Goff Ailloud, declaró que la corrupción afecta y vulnera los derechos humanos de los sectores más vulnerables, como las mujeres, los pobladores mayas, los niños y las personas de la tercera edad, por lo que tiene que ser sancionada para evitar repercusiones severas. <br>
          “Decir que se puede acabar la corrupción es una utopía, pero sí se puede combatir y reducir los índices”, destacó, al momento que explicó que hay que desmitificar que este flagelo es parte de la cultura mexicana. <br>
          Por su parte, el comisionado presidente del Instituto Estatal de Transparencia (INAIP), Aldrín Briceño Conrado, sostuvo que la transparencia es una herramienta poderosa y fortalecida para detectar actos de corrupción e inhibirlos. <br>
          Recalcó que es deber de la ciudadanía señalar los actos de discrecionalidad de los servidores públicos y que hay que generar conciencia de la ética pública desde la premisa que este fenómeno daña a terceras personas. <br>
          En su intervención, el presidente del Tribunal de Justicia Administrativa de Yucatán, Miguel Diego Barbosa Lara, indicó que hay que fortalecer el trabajo coordinado y conjunto de las instituciones para luchar contra este flagelo. <br>
          Asimismo, el magistrado presidente del Tribunal Superior de Justica, Ricardo Ávila Heredia, dijo que si bien las acciones de los jueces hablan a través de sus sentencias, esas hay que complementarlas con apertura, sensibilidad y atención a los usuarios, ya que con la transparencia el Poder Judicial se legitima ante la sociedad. <br>
          En el encuentro, la titular de la Unidad de Contraloría Municipal de Mérida, Martha Gómez Nechar, y la jefa del departamento de Comunicación y Vinculación de la Secretaría de Contraloría de Yucatán, Aida Borges Aguilar, coincidieron en la importancia de devolver la confianza de las instituciones a la ciudadanía a través de mecanismos que funcionen para denunciar y evitar actos de corrupción. <br>
          La mesa de diálogo se llevó a cabo en el Centro Cultural Olimpo y formó parte de las actividades de la Semana Anticorrupción. Al acto asistieron los integrantes del Comité de Participación Ciudadana, Mónica Febles Álvarez Icaza, Javier Montes de Oca Zentella, Manuel Castillo Rendón y José Luis Villamil Urzáiz, así como el secretario técnico del Sistema, Edwin Rejón Pacheco. 

