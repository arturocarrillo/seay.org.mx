         En punto de las 11:00 hrs. vía Stream Yard, este miércoles 11 de marzo de 2021, y por invitación de la Universidad Privada de la Península, en el marco del Grow Up online 2021, se llevó a cabo la conferencia: “Hablemos de corrupción”, a cargo del Mtro. Luis Guillermo Juárez Ramírez, Jefe de Comunicación de la SESEAY.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
         Después de llevar a cabo el acto protocolario de bienvenida, se cedió la palabra al Mtro. Juárez, el cual comenzó agradeciendo a nombre del Sistema Estatal Anticorrupción de Yucatán a las autoridades de la institución la invitación y recordó el Convenio de Colaboración que ambas instancias tienen firmado, además del Comité de Ética Estudiantil que ya está en funciones desde el año pasado.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          La charla comenzó haciendo diversas preguntas a los asistentes, con la intención de sensibilizar sobre la importancia de observar una conducta ética y responsable en nuestra vida personal, la cual se verá reflejada en su vida profesional, mencionó la importancia de tener una reputación escolar positiva la cual en un futuro nos abrirá las puertas en el mercado laboral, ya sea como colaboradores de una organización o como empresarios.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          La audiencia tuvo la oportunidad por medio de preguntas de interactuar con el ponente, lo cual permitió tocar temas como: la responsabilidad social, la importancia de ser buenos “escuchas”, habló sobre la diferencia entre oír y escuchar, la solidaridad, la actitud empática, la honestidad, tolerancia, el respeto, entre otros.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          La conferencia se centró en crear conciencia en la importancia de tener una actitud íntegra, entender la diferencia entre ser honestos e íntegros, e instó a los presentes a mantener una conducta congruente entre lo que lo que piensan, lo que dicen, con lo que hacen. También hizo mucho énfasis en mantener una actitud participativa en la vida social, llevando a cabo una actitud de denuncia ante las acciones que sabemos que no son correctas, invitó a conocer y utilizar el “mecanismo de queja” que se brinda en la SESEAY, y a informarse sobre los servicio que brinda el SEAY.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          El Mtro. Juárez por último, invitó a todos los presentes a tener una actitud congruente entre los valores y principios que posee y su actuar a nivel cotidiano, a recordar que “no somos perfectos”, que tenemos todo el derecho a equivocarnos, pero debemos mantener una actitud responsable ante nuestros actos, que a medida que todos colaboremos podremos erradicar este cáncer de nuestra sociedad llamado corrupción.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          La conferencia culminó a las 12: 30 aproximadamente, con el agradecimiento de la Mtra. Laura Espadas Herrera, Directora de Vinculación Interinstitucional, la cual a nombre del SEAY, agradeció la invitación y la actitud participativa y responsable que ha mostrado la UPP de manera permanente, y les recordó que mantendremos la actitud participativa y colaborativa en todo momento.
