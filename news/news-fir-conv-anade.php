				<p class="paragraph-md text-blue_1 p-2">
					Jueves 19 de septiembre de 2019, en punto de las 16:00 horas en las instalaciones de la SESEAY, se llevó a cabo la firma del Convenio de colaboración entre la propia institución y la Asociación Nacional de Abogados de Empresas (ANADE).
				</p>
				<p class="paragraph-md text-blue_1 p-2">
          Después de un breve acto protocolario, cedieron la palabra en primera instancia a la M. en D. Graciela Alejandra Torres Garma, Presidenta del Sistema Estatal Anticorrupción, la cual refrendó el compromiso de colaborar en forma permanente con la institución y reconoció el gran esfuerzo administrativo por parte de ambas instancias para que este convenio se llevara a cabo, reconoció la trayectoria de la ANADE y su reputación en relación a sus procesos de transparencia, posteriormente el Presidente Nacional de la Asociación Nacional de Abogados de Empresas, el Lic. Adrián Ojeda Cuevas, exhortó a ambos actores del convenio celebrado a trabajar de manera coordinada y sinérgica en pro del desarrollo integral de los abogados de la entidad, a continuación, el Lic. Mario Arturo Romero Sosa, Presidente del Comité Directivo de la Sección Yucatán, el cual hizo mucho énfasis en no permitir que el convenio sea un documento que solo se firme y se archive, por lo que se comprometió a trabajar de manera coordinada y conjunta con la SESEAY. Por último, el Lic. Edwin Manuel Rejón Pacheco, Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción, reconoció el esfuerzo que ambas instancias realizaron para consolidar un documento que beneficie a ambas instancias y reafirmó el compromiso por parte de la SESEAY por coadyuvar al logro de los objetivos planteados por ambas instituciones.
				</p>
        <p class="paragraph-md text-blue_1 p-2">
          En el acto protocolario estuvieron presentes el Arq. Manuel Castillo Rendón, miembros del Comité de Participación Ciudadana, así como la Lic. Laura Espadas Herrera, Directora de Vinculación Interinstitucional de la SESEAY; y por la ANADE, el Lic. Israel Imán Baquedano, Primer Vicepresidente y Coordinador del Comité de Anticorrupción, la Lic. Karen Leticia Caro Leal, Segunda Vicepresidenta, la Lic. Regina Marín Méndez, Tesorera y el Lic. Hebert Martínez, Coordinador de Comités de trabajo. 
