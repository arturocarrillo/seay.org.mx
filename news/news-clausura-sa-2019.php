      <div class="w-100 mt-5 mb-5 flex">
        <h2 class="title text-blue_1">Ciudadanía es fundamental para la cultura anticorrupción <br> Conmemoran Día Internacional contra la Corrupción</h2>
        <p class="paragraph-md text-blue_1 p-2">
          Mérida, Yucatán, a 9 de diciembre de 2019.- La transformación de la administración pública, para que el ejercicio de los recursos sea con mayor transparencia y se sancione a quienes utilicen sus cargos y poder para beneficio personal, necesariamente pasa por la construcción de políticas públicas que involucren directamente a la ciudadanía, advirtió la presidenta del Sistema Estatal Anticorrupción de Yucatán (SEAY), Graciela Alejandra Torres Garma.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          En el marco del Día Internacional contra la Corrupción, el Comité Coordinador del SEAY tuvo una sesión solemne, realizada en el Auditorio “Víctor M. Cervera Pacheco” del Tribunal Superior de Justicia del Estado de Yucatán (TSJEY), donde Torres Garma sostuvo que el énfasis de la labor que realizan está centrado en las y los ciudadanos.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          “Por eso hablamos de políticas públicas, cuyo énfasis está centrado en la participación y la consulta a la ciudadanía, porque sólo así, al escuchar las voces de quienes somos iguales ante la ley, podemos elaborar directrices que guíen y regulen el quehacer de la función pública para evitar actos de corrupción”, afirmó.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          En ese sentido, destacó que al corte de resultados de la encuesta en línea para la Política Estatal Anticorrupción, se tenían un total de 922 respuestas, que serán una de las bases para fortalecer una cultura anticorrupción.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          “Ese instrumento, nos ha permitido detectar las principales demandas de los yucatecos con respecto a los 60 temas prioritarios para nuestro Sistema Estatal”, subrayó.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          En su mensaje, la también presidenta del Comité de Participación Ciudadana (CPC) del SEAY mencionó que el Estado de derecho y el combate a la corrupción son considerados en el orden internacional como herramientas necesarias para atender la pobreza y la desigualdad en América Latina.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Según datos de la Organización de las Naciones Unidas (ONU), abundó, cada año se paga un billón de dólares en sobornos y se calcula que se roban 2.6 billones de dólares anuales mediante la corrupción, suma que equivale a más del cinco por ciento del producto interno bruto mundial.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Ante Mónica Febles Álvarez Icaza, Javier Montes de Oca Zentella, Manuel Castillo Rendón y José Luis Villamil Urzáiz, también integrantes del CPC, la presidenta del SEAY opinó que sólo tenemos el presente para hacer que el futuro sea distinto.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          “Sólo hoy podemos trazar una cartografía que nos permita navegar hacia nuevos horizontes, donde las siguientes generaciones construyan sobre los cimientos de la cultura anticorrupción”, finalizó.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          En el acto estuvieron presente el secretario ejecutivo del SEAY, Edwin Rejón Pacheco; así como los integrantes del Comité de Coordinación del Sistema: Lizbeth Basto Avilés, titular de la Secretaría de la Contraloría General; Ricardo Ávila Heredia, presidente del Consejo de la Judicatura y del TSJEY.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Asimismo, los titulares del Tribunal de Justicia Administrativa del Estado y de la Auditoría Superior, Miguel Diego Barbosa Lara y Mario Can Marín, respectivamente; también acudieron el comisionado presidente del INAIP, Aldrin Briceño Conrado; y el vicefiscal Anticorrupción, José Enrique Goff Allioud.
        </p>
