			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
					 Martes 08 de agosto de 2020, en punto de las 11:00 hrs. en el auditorio del edificio “M” del Instituto Tecnológico Superior de Motul, se llevó a cabo la firma de convenio de colaboración entre la Secretaría Ejecutiva del Sistema Estatal Anticorrupción (SESEAY) y el Instituto Tecnológico Superior de Motul, los firmantes, por parte del ITSM: el Ing. Isaías Sosa Avilés. Director General; y por parte de la SESEAY, la M. en F. Mónica Alicia Febles Álvarez-Icaza, Presidente del Sistema Estatal Anticorrupción de Yucatán. 
				</p>
				<p class="paragraph-md text-blue_1 p-2">
          En primera instancia, tomó la palabra el Director General del ITSM, el Ing. Sosa Avilés, el cual reconoció el esfuerzo de ambas partes en consolidar la firma del convenio tomando en cuenta los obstáculos que estamos viviendo hoy a nivel global, como lo es la pandemia de COVID-19, agradeció a su personal el empeño puesto en sus labores cotidianas, invitó a que se unan esfuerzos permanentes para hacer que el convenio sea realmente una unión sinérgica entre ambas instancias; fue el turno de la M. en F Febles Álvarez-Icaza, presidenta del Sistema Estatal Anticorrupción, la cual en primera instancia mencionó el importante papel que juega el sistema Estatal Anticorrupción como representante de la sociedad civil ante la denuncia de presuntos actos de corrupción, reconoció los valores de la institución y las acciones que han llevado a cabo en los últimos en apoyo a la sociedad civil en relación a la pandemia y por último invitó a todos los presentes a comprometernos en esta lucha frontal contra la corrupción. 
				</p>
        <p class="paragraph-md text-blue_1 p-2">
          Finalmente se impartió la plática informativa: “Sistema Estatal Anticorrupción de Yucatán, funcionamiento y servicio” impartida por el Mtro. Luis Guillermo Juárez Ramírez, jefe del departamento de comunicación Social de la SESEAY, en la que el maestro tuvo la oportunidad de explicar el funcionamiento del Sistema Estatal Anticorrupción y mencionar los servicios que se ofrecen, en esta caso y en particular a las instancias educativas de la entidad, plática presencial con todas las medidas precautorias de sana distancia y protocolo de sanitización, dirigido a personal administrativo de la instancia educativa. 
        </p>
