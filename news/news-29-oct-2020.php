      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Éste jueves 29 de octubre de 2020, en punto de las 18:00 horas, vía ZOOM se llevó a cabo el primer foro de opinión del Consejo Consultivo Anticorrupción, en el que se presentaron diversas propuestas por parte de los integrantes de este conglomerado de expertos en torno a la propuesta de la Política Estatal Anticorrupción del Estado de Yucatán.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          En primera instancia, el maestro de ceremonias llevó a cabo el acto protocolario de bienvenida y acto seguido, presentó a todos y cada uno de los 20 integrantes del Consejo Consultivo, solicitándoles a manera de pase de lista, levantar la mano cuando escucharan su nombre, posteriormente procedió a nombrar a los cinco integrantes del Comité de Participación Ciudadana y al Secretario Técnico de la SESEAY. 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
         Después de un breve preámbulo por parte del maestro de ceremonias sobre el objetivo del Consejo Consultivo y de este primer ejercicio de opiniones y propuestas sobra la PEAY, cedió el micrófono a la moderadora del foro, la Mtra. Mónica Alicia Febles Álvarez-Icaza, la cual, después de un breve mensaje a los presentes, explicó la dinámica del foro de opinión y solicitó los interesados en participar hacerlo saber.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Uno a uno, los interesdos en participar, hasta en una segunda vuelta, tuvieron la oportunidad de exponer su análisis y sus diversas propuestas en el foro, cada intervención tuvo una duración de hasta dos minutos. 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          Una vez realizadas las dos rondas de participación, y con la intervención de la moderadora, la Mtra. Febles, Presidenta del Comité de Participación Ciudadana y del Sistema Estatal Anticorrupción, se dio por cerrado el ejercicio de opinión y propuestas en torno a la Propuesta de la Política Estatal Anticorrupción, se acordó continuar en comunicación permanente para compartir información y la relatoría del evento.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
          A las 19:30 aproximadamente se dio por concluída la sesión.
