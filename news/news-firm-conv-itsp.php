			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
            Miércoles 09 de octubre de 2019, en punto de las 11:00 hrs. en el aula magna del Instituto Tecnológico Superior de Progreso, se llevó a cabo la firma de convenio de colaboración entre la Secretaría Ejecutiva del Sistema Estatal Anticorrupción (SESEAY) y el Instituto Tecnológico Superior de Progreso (ITSP), los firmantes, por parte del ITSP: el Lic. Rodolfo Enrique González Crespo. Director General, la Arq. María del Carmen Sánchez González Subdirectora Académica y Vinculación, el C.P. Ricardo Agustín Paz Alonzo, Subdirector Administrativo; y por parte de la SESEAY, la M. en D. Graciela Alejandra Torres Garma, Presidente del Sistema Estatal Anticorrupción de Yucatán y la Mtra. Laura Espadas Herrera, Directora de Vinculación Interinstitucional. 
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					  En primera instancia, tomó la palabra el Director General del ITSP, el Lic. González Crespo, el cual enfatizó la importancia de firmar este tipo de convenios y conminó a ambas instancias a trabajar de manera estrecha y constante para la consolidación de una sociedad más justa y equitativa, reconoció el esfuerzo por parte de ambas instancias para la consolidación del Convenio; fue el turno de la M. en D. Torres Garma, presidenta del Sistema Estatal Anticorrupción, la cual agradeció en primera instancia la invitación a firmar en sus instalaciones dicho convenio y se comprometió a trabajar de manera conjunta y permanente en la lucha frontal contra la corrupción de la mano con el ITSP. 
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					Finalmente se impartió la conferencia: “Hablemos de corrupción” impartida por el Mtro. Luis Guillermo Juárez Ramírez, jefe del departamento de comunicación Social de la SESEAY, en la que el maestro tuvo la oportunidad de reflexionar sobre las consecuencias e implicaciones de una conducta corrupta, la importancia de tener una actitud responsable ante tal fenómeno y la importancia de tener una formación integral, con valores y ética en su ejercicio personal y profesional.  <br>
          En el evento estuvieron presentes, el Arq. Manuel J. Castillo Rendón, integrante del Comité de Participación Ciudadana, y la Lic. Patricia Prieto Sánchez, jefe del departamento de control de indicadores de la SESEAY.
				</p>
