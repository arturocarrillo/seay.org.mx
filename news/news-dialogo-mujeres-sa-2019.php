				<h2 class="title text-blue_1">Reseña de la mesa de diálogo "Encuentro con Mujeres Empresarias"
			</div>
			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
            Mérida, Yucatán, 4 de diciembre de 2019. Capacitar al personal de las compañías, fomentar valores como la integridad y la honestidad, trabajar con el ejemplo, fomentar la cultura de la denuncia y exhortar a las autoridades a simplificar los trámites para echar andar negocios, fueron algunas de las propuestas para combatir la corrupción que plantearon empresarias yucatecas que participaron en el foro organizado en la Semana Anticorrupción. <br>
            La mesa de diálogo denominada “Encuentro con mujeres empresarias”, que fue transmitida por redes sociales, estuvo moderada por Rosa María Cruz Lesbros, integrante del Comité de Participación Ciudadana Nacional del Sistema Anticorrupción, quien enfatizó que la lucha contra este flagelo que impacta a nuestra sociedad es un ejercicio permanente porque el riesgo estará siempre latente en diferentes esferas sociales. <br>
            En ese sentido, pidió a las participantes del encuentro un intercambio de ideas sobre cuáles estrategias podían implementar desde sus empresas para generar un cambio positivo. <br>
            Al respecto, Cinthya Hueso Rosado precisó que la corrupción no distingue género, pero recalcó que las mujeres son más vulnerables ante este tipo de actos, por lo que se pronunció a favor de que la honestidad sea el estandarte inamovible y que éste sea firme e intachable en la vida profesional. <br>
            También dijo que es necesario difundir en qué consisten las leyes y cuáles son los mecanismos para interponer quejas o denuncias cuando detecten casos de corrupción. De igual forma, destacó el trabajo que realiza el Sistema Estatal Anticorrupción de Yucatán, el cual no debe verse como un castigo, sino como un avance en la sociedad para evitar actos ilícitos. <br>
            Por su parte, la empresaria Gloria Campos Calderón señaló que hay que apostarle a la capacitación de las y los trabajadores de sus negocios sobre por qué es importante no caer en este tipo de hechos y, además, crear sistemas internos de denuncias, tanto de su propio personal como de los clientes. <br>
            Mientras tanto, la licenciada Cristina Arellano Guerrero hizo énfasis en llevar a cabo acciones para detener el acoso sexual, pues es una práctica lamentable que suele ocurrir en las empresas y que, incluso, se cree que es una “manera para que las mujeres puedan escalar posiciones”. <br>
            En su intervención, la doctora Neyi Estrella Gómez puso en la mesa el tema de que el proceso para denunciar actos de corrupción representa un gasto emocional <br>y energético muy fuerte, sobre todo cuando se enfrentan a grandes empresas e instituciones, por lo que dijo que es necesario que haya un acompañamiento a las denunciantes. “Por eso es importante que se haya echado andar el Sistema Anticorrupción, para que no nos sintamos solas”, declaró. <br>
            Asimismo, María del Carmen Vidal Castro y Karla Cardoso Uribe coincidieron en fomentar valores y educación tanto en los hogares como en las empresas, para poner cimientos fuertes a las nuevas generaciones y así evitar que caigan en actos de corrupción en el futuro. <br>
            En el encuentro con Mujeres Empresarias participó Mónica Febles Álvarez-Icaza, integrante del Comité de Participación Ciudadana del Sistema Estatal Anticorrupción, quien enfatizó que hay que impulsar la cultura de la denuncia para que estos actos no queden impunes, sobre todo para que quede constancia de que sí hay repercusiones para quienes los cometen. <br>
            Por su parte, la presidenta del Comité, Graciela Torres Garma, sostuvo que el ejercicio diario de la honestidad y las buenas prácticas en todos los ámbitos en los que se desarrollan las mujeres, se puede replicar entre los hijos, estudiantes y trabajadores de sus empresas. <br>
            “Cuando vean que a la gente honesta le va mejor, ellos mismos lo van a replicar. La corrupción es una montaña ficticia que se va a caer, en cambio la honestidad es a largo plazo, es una escalera que tiene cimientos fuertes”, finalizó. <br>
            Como parte de las conclusiones de dicha mesa, las empresarias indicaron que las dependencias gubernamentales deben implementar procesos más ágiles en los trámites y permisos, sobre todo cuando se trata de abrir nuevos negocios, ya que eso inhibe actos de corrupción.
				</p>
