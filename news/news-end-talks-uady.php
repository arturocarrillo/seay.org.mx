					 <span class="bold">Reseña de la Conferencia: ¿Qué onda con la corrupción? a estudiantes de la Preparatoria No. 1 de la UADY.</span><br>
           Ante un auditorio completamente lleno y un gran entusiasmo, se llevó a cabo este <span>martes 1 de octubre de 2019</span>, en el auditorio de la preparatoria No. 1 de la UADY la conferencia: “¿Qué onda con la corrupción?” impartida por el Mtro. Luis Guillermo Juárez Ramírez, jefe del departamento de comunicación Social de la SESEAY, en ésta, los alumnos tuvieron la oportunidad de reflexionar sobre las diferentes implicaciones que conlleva una conducta irresponsable y corrupta. Con la presencia y apoyo de la Directora de Vinculación Interinstitucional de la SESEAY, la Mtra. Laura Espadas Herrera, se aplicaron las encuestas a cada uno de los 241 alumnos que se dieron cita en el recinto; cabe mencionar que los alumnos manifestaron sus inquietudes sobre el propio SEAY y su interés por abordar más temas relacionados a la corrupción u otros temas similares.

				</p>
