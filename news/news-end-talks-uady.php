            <span class="bold">Reseña de la Conferencia: ¿Qué onda con la corrupción? a estudiantes de la Preparatoria No. 2 de la UADY.</span><br>
            En punto de las 11:40 horas en el Auditorio de la Preparatoria No. 2 del <span>lunes 30 de septiembre de 2019</span>, se llevó a cabo la conferencia “¿Qué onda con la corrupción?” impartida por el Mtro. Luis Guillermo Juárez Ramírez, jefe del departamento de comunicación Social de la SESEAY, estudiantes del tercer año, se  dieron cita en el recinto para escuchar la plática en la que el maestro tuvo la oportunidad de reflexionar sobre las consecuencias e implicaciones de una conducta corrupta, la importancia de tener una actitud responsable ante tal fenómeno y lo importante de tener una formación integral, con valores y ética en su ejercicio personal y profesional. <br>
            En la plática estuvo presente la Mtra. Laura Espadas Herrera, Directora de Vinculación Interinstitucional de la SESEAY, refrendó con los alumnos el compromiso formal de colaborar con las instituciones y aplicó la encuesta que permite a las SESEAY rescatar las inquietudes y deseos de colaborar con la institución.

				</p>
