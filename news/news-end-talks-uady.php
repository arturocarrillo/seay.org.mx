           <span>Miercoles 2 de octubre de 2019</span>, en punto de las 14 horas los alumnos del tercer año de bachillerato escucharon atentos la explicación sobre el Sistema Estatal Anticorrupción y reflexionaron sobre las implicaciones que la corrupción tiene a nivel personal y profesional en sus vidas. Cabe destacar la presencia de la Lic. Mónica Febles Álvarez Icaza, integrante del Comité de Participación Ciudadana, la cual dio testimonio de su incursión en el Sistema, de su experiencia profesional y aportó consejos y sugerencias para evitar fomentar este fenómeno. La Mtra. Laura Espadas Herrera, Directora de Vinculación Interinstitucional de la SESEAY llevó a cabo la consulta mediante las encuestas en las que se recogen todas las inquietudes de los alumnos.
