        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-2">
          En punto de las 10:00 hrs., este miércoles 17 de marzo del presente año, en modalidad presencial, con todas las medidas preventivas y de sana distancia, además de un enlace virtual vía ZOOM, se llevó a cabo la rueda de prensa de la presentación del Observatorio Electoral ILEJ a los medios masivos de comunicación de nuestra entidad.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Después del acto protocolario de bienvenida a todos los medios de comunicación reunidos en las instalaciones de la SESEAY por parte del maestro de ceremonias, se realizó la presentación de las tres representantes de la mesa de honor: Mtra. Gilda Segovia Chab, Comisionada Presidenta del Instituto Estatal de Transparencia, Acceso a la Información Pública y Protección de Datos Personales INAIP Yucatán, Mtra. María de Lourdes Rosas Moya, Consejera Presidente del Instituto Electoral y de Participación Ciudadana de Yucatán (IEPAC)  y la Mtra. Mónica Febles Álvarez-Icaza, Presidente del Comité de Participción Ciudadana y del SEAY.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Se presentaron también a todos los Consejeros Técnicos del IEPAC, uno a uno se mencionaron sus nombres:
        </p>
        <ol class="w-90 text-md text-green-s1 font-weight-normal p-2 m-auto">
          <li>Dr. Aldrin Briceño Conrado (Comisionado del INAIP)</li>
          <li>Dr. Carlos Fernando Pavón Durán (Comisionado del INAIP)</li>
          <li>M.D.P. Delta Alejandra Pacheco Puente (Consejera IEPAC)</li>
          <li>Lic. Jorge Antonio Vallejo Buenfil (Consejero IEPAC)</li>
          <li>Mtra. María del Mar Trejo Pérez (Consejera IEPAC)</li>
          <li>Mtra. Alicia del Pilar Lugo Medina (Consejera IEPAC)</li>
          <li>Mtro. Alberto Rivas Mendoza (Consejero IEPAC)</li>
          <li>Lic. Roberto Ruz Sahrur (Consejero IEPAC)</li>
          <li>Mtro. Hidalgo Armando Victoria Maldonado (Secretario Ejecutivo IEPAC)</li>
          <li>LCC. Javier Montes de Oca Zentella (Integrante del C.P.C)</li>
          <li>Dra. Ivanna Ongay Xacur (Integrante del C.P.C)</li>
          <li>Arq. Manuel de Jesús Castillo Rendón (Integrante del C.P.C)</li>
          <li>Dr. José Luis Villamil Urzaiz (Integrante del C.P.C)</li>
          <li>Lic. Edwin Rejón Pacheco (Secretario Técnico de la SESEAY)</li>
        </ol>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          En primera instancia, la Mtra. Mónica Febles presentó los objetivos del Observatorio Electoral, la justificación y los beneficios que tendrá este observatorio para la ciudadanía, presentó la contribución del Comité de Participación Ciudadana, la necesidad de que los candidatos presentes sus declaraciones 3 de 3 y la explicación de los compromisos anticorrupción dentro de todo el proyecto.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          En el turno de la Mtra. Gilda Segovia Chab, Comisionada Presidenta del Instituto Estatal de Transparencia, Acceso a la Información Pública y Protección de Datos Personales INAIP Yucatán, presentó la contribución que hará el INAIP en el proyecto, específicamente en relación con los compromisos de transparencia, destacó la contribución que realizó el Edo. De  Jalisco para aportar en el desarrollo de la plataforma y agregarla al micrositio, además de enfatizar en la importancia de incluir en éste proyecto a los 106 municipios del Estado, y hacer llegar la información oportuna y llevar a cabo las diligencias necesarias para aportar en las infraestructura tecnológica, y así todos los ciudadanos del Estado puedan participar del mismo.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Durante la participación de la Mtra. María de Lourdes Rosas Moya, Consejera Presidente del Instituto Electoral y de Participación Ciudadana de Yucatán (IEPAC), mencionó la contribución del Instituto que ella representa en el desarrollo del observatorio, y realizó especial énfasis en la declaración 3 de 3 de igualdad de género, mencionó la importancia de establecer un proceso en el que la igualdad y equidad en los procesos sea sumamente transparente.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Fue el turno del Mtro Eduardo Bohórquez, Director General de Transparencia Mexicana, el cual mediante un mensaje grabado, se dirigió a los presentes mandando un mensaje de apoyo permanente e incondicional a las tres instituciones que están realizando un esfuerzo constante y permanente por darle herramientas a la ciudadanía para que emitan un voto consciente, reiteró el apoyo por parte de Transparencia Mexicana e instó a todos los que integran este importante proyecto a mantener ese mismo entusiasmo durante el desarrollo del proyecto.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Acto seguido, las autoridades de las tres instancias participantes invitaron a los reporteros a realizar preguntas, respondieron a cada una de los cuestionamientos, para luego dar paso a dar la información sobre las fechas importantes, y detallar sobre la información que se estará compartiendo con la ciudadanía una vez que se vayan presentando los resultados.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Por último, las representantes de las tres instancias agradecieron a todos los integrantes de la prensa escrita y digital su presencia esa mañana en las instalaciones de la SESEAY. Acto seguido procedieron a responder las preguntas de la prensa de manera personal.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          La rueda de prensa culminó aproximadamente a las 10:40, con la despedida por parte de la Presidenta del SEAY, la Mtra. Mónica Febles y el agradecimiento a todos los presentes.
