        <h2 class="title text-blue_1">Arranca la Semana Anticorrupción para ampliar el debate sobre cómo
combatir este flagelo</h2>
      </div>
      <div class="w-100 mt-5 mb-5 flex">
        <p class="paragraph-md text-blue_1 p-2">
          Activos 24 expedientes de quejas presentadas por la ciudadanía sobre posibles
casos de corrupción, informó la presidenta del Comité de Participación
Ciudadana del Sistema Estatal Anticorrupción de Yucatán, Mtra. Graciela
Alejandra Torres Garma.
Mérida, Yucatán, 3 de diciembre de 2019. Construir de la mano de la ciudadanía
políticas públicas que consoliden una cultura anticorrupción y ampliar el debate
social sobre cómo mitigar este flagelo que tanto le ha costado a México y a
Yucatán, son de los asuntos más relevantes que se abordan en la Semana
Anticorrupción, que arrancó formalmente este martes y concluirá el próximo
lunes 9 de diciembre. <br>
Así lo destacó la presidenta del Comité de Participación Ciudadana del Sistema
Estatal Anticorrupción, Graciela Alejandra Torres Garma, quien enfatizó que a
través de estas acciones buscan que las autoridades cumplan a cabalidad con
sus encomiendas, porque eso hará que los ciudadanos recobren la credibilidad
en las instituciones. <br>
Como ejemplo del trabajo realizado desde principios de este año por el Sistema
Anticorrupción en Yucatán, informó que la Secretaría Ejecutiva atiende 24 casos
presentados por ciudadanos a través del Mecanismo de Queja, relacionados con
investigaciones a instancias estatales y municipales.
Torres Garma detalló que de los expedientes activos y en trámite: nueve están
en proceso de prevención y admisión, otros siete en la etapa de informe de la
autoridad y cinco más se encuentran en análisis de conclusión anticipada. Uno
está en la etapa de audiencia aclaratoria y otro en el periodo probatorio. Uno más
está en el proceso de determinación. <br>
Con respecto a las otras tres quejas, explicó que fueron cerradas y
archivadas por no ser competencia de la Secretaria Ejecutiva o por ser
notoriamente improcedente, al no existir elementos para dar trámite.
Por su parte, el secretario ejecutivo del Sistema, Edwin Rejón Pacheco, abundó
que la intención de recibir estas quejas es analizar cada uno de los casos y darle
vista a las autoridades para que puedan aclarar a los ciudadanos cualquier
situación que no les quede de todo claro y, recalcó, en este proceso pueden
emitir recomendaciones públicas no vinculantes a las instituciones.
Además, precisó, de advertirse responsabilidades administrativas o hechos de
corrupción, también recurren a las autoridades competentes para que continúen
con su proceso jurisdiccional o administrativo. <br>
03 de diciembre 2019
“Como Sistema Anticorrupción buscamos que no se vuelva a repetir el hecho, detener este ciclo vicioso en la administración pública, independientemente de que se castigue a los responsables”, agregó Rejón Pacheco.
Con respecto a las actividades y eventos programados para esta Semana Anticorrupción, destaca la conferencia magistral “La transparencia como herramienta para combatir la corrupción”, impartida por el presidente del INAIP, Aldrín Briceño Conrado; así como la denominada “Responsabilidad penal de la persona jurídica derivada de la corrupción”, por los abogados Moisés Castro Pizaña y Miguel Ángel Hernández de Alba, integrantes de ANADE.
También se realizará la mesa de diálogo “Reunión pública con universitarios” que se transmitirá en vivo desde la página de Facebook @cpcyucatan.org.mx; y el coloquio con estudiantes “Hablemos de Anticorrupción” en la Facultad de Derecho de la UADY y un encuentro con mujeres empresarias que también será transmitido en vivo.
De igual forma, el diálogo “Acciones y propuestas anticorrupción” por parte del comité coordinador de la SAEY y, para finalizar, la conferencia magistral “El impacto de la política pública anticorrupción en las estadísticas”, impartida por el doctor Oscar Jaimes Bello, director general de Estadísticas de Gobierno, Seguridad Pública y Justicia-INEGI. <br>
La presidenta Torres Garma destacó que durante este año el Sistema Anticorrupción suscribió 22 convenios de colaboración con asociaciones civiles, cámaras empresariales, universidades, el Ayuntamiento de Mérida y con la Comisión de los Derechos Humanos del Estado de Yucatán (CODHEY) para sumar esfuerzos en el combate al flagelo social.
Asimismo, emprendieron una intensa campaña de difusión en radio, en la que se transmitirán un total de 5360 spots, es decir, más de 15 horas de tiempo aire, que fue otorgado sin costo alguno por las principales radiodifusoras estatales, para fortalecer las acciones del Sistema.
Además, se habilitó un sistema de consulta para la construcción de la Política Estatal Anticorrupción de Yucatán, para lo cual se diseñó una encuesta con las 60 prioridades que habrán de atenderse en esta importante labor. En un primer corte, se tienen registradas 900 encuestas contestadas en línea, lo que da cuenta del interés de la ciudadanía en aportar en este objetivo. <br>
En el encuentro con medios de comunicación también estuvieron los demás integrantes del Comité de Participación Ciudadana: Mónica Febles Álvarez Icaza, Javier Montes de Oca Zentella, Manuel Castillo Rendón y José Luis Villamil Urzaiz.
La información completa del programa de la Semana Anticorrupción está disponible en el sitio www.seay.org.mx
