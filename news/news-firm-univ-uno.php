			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
					 En punto de las 11:30 a.m. este miércoles 16 de octubre de 2019, en la sala de juntas de la SESEAY, se llevó a cabo la firma del convenio de colaboración entre la Universidad de Oriente (UNO) de Valladolid, y la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY). <br>
           Después de llevar a cabo el acto protocolario de bienvenida por parte de la SESEAY y presentar a los miembros de la mesa de honor:
				</p>
				<ol class="paragraph-md w-90 text-blue_1 p-2">
					 <li>Lic. Luis René Fernández Vidal, Rector de la Universidad de Oriente.</li>
           <li>C.P Amireyoly Burgos Aguilar, Directora de Planeación Institucional de la Universidad de Oriente</li>
           <li>Lic. Edwin Manuel Rejón Pacheco, Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán. </li>
           <li>Mtra. Graciela Alejandra Torres Garma, Presidente del Sistema Estatal Anticorrupción de Yucatán.</li>
				</ol>
				<p class="paragraph-md text-blue_1 p-2">
					Se procedió a ceder la palabra al Lic. Fernández Vidal, rector de la Universidad de Oriente, el cual hizó hincapié en las actividades que como Institución educativa están llevando a cabo, entre las que destaca la conformación de diferentes Comités estudiantiles que han conformado con la firme intención de organizar y dotar a la comunidad de voz propia en la conformación de una identidad institucional que se forme basada en principios y valores institucionales sólidos. Destacó la importancia de firmar documentos de colaboración mutua que sumen en el desarrollo integral de los estudiantes. Posteriormente la Presidenta del Sistema Estatal Anticorrupción, la M. en D. Graciela Alejandra Torres Garma subrayó los esfuerzos de las instancias del interior del Estado por firmar convenios de colaboración institucionales, en particular con la Universidad de Oriente destacó los esfuerzos por llevar una formación integral de sus alumnos. <br>
          Después de firmar el convenio correspondiente inició la sesión de preguntas y respuestas por parte de la prensa, alrededor de las 12:30 finalizó el acto.  
