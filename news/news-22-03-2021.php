      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Este lunes 22 de marzo del presente año, a las 11:00 a.m., en las instalaciones del INAIP Yucatán, la Mtra. Mónica Febles Álvarez-Icaza, representando al Comité de Participación Ciudadana (CPC) del Sistema Estatal Anticorrupción de Yucatán (SEAY), en colaboración con la Mtra. Gilda Segovia Chab, Comisionada Presidente del Instituto Estatal de Transparencia, Acceso a la Información Pública y Protección de Datos Personales (INAIP Yucatán), la Mtra. María de Lourdes Rosas Moya, Consejera Presidente del Instituto Electoral y de Participación Ciudadana del estado de Yucatán (IEPAC) y Transparencia Mexicana, realizaron la presentación de la plataforma del Observatorio  Electoral “ILEJ”, así como a la firma de Convenio General de colaboración entre el partido que representa y las instituciones implementadoras.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          En primera instancia la Mtra. Segovia Chab, después de agradecer a todos los asistentes de manera presencial y virtual, entre los que se encontraban los representantes de 11 partidos políticos e integrantes del Comité de Participación Ciudadana del SEAY, además de tener como testigos de honor al Mtro. Eduardo Bohórquez, Director General de Transparencia Mexicana y el Lic. Fernando Ponce Díaz, Presidente del Consejo Coordinador Empresarial en nuestro Estado, procedió a explicar la importancia de la plataforma y mencionó las aportaciones que este mecanismo traerá a todos los ciudadanos, remarcó que la construcción del micrositio diseñado por el grupo de trabajo del Observatorio Electoral ILEJ, es posible gracias a las facilidades otorgadas por el Instituto de Transparencia, Información Pública y Protección de Datos Personales de Jalisco (ITEI), creador de la Plataforma 3 de 3 por la Integridad.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Por su parte, la Mtra. Febles Álvarez-Icaza, Presidenta del CPC del SEAY, presentó de manera detallada la conformación del micrositio del observatorio electoral ILEJ,  y enfatizó sobre la oportunidad que tienen todos los ciudadanos de acceder a esta información de manera clara y sencilla y así contar con la posibilidad de dar un voto razonado.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Mediante vía remota,  la Comisionada Presidenta del ITEI, Cynthia Patricia Cantero Pacheco, hizo mención de la importancia del esfuerzo para consolidar la coordinación entre las instancias participantes y reafirmó su compromiso de trabajar de la mano con las organizaciones públicas de la entidad.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Posteriormente, el Lic. Eduardo Bohórquez, Director de Transparencia Mexicana, envió un mensaje en el que enfatizó la cifra de. 1.6 millones de electores, los cuales tendrán elementos suficientes para poder emitir un voto más consciente y razonado.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Por último, la consejera presidenta del IEPAC, María Lourdes Rosas Moya, en su breve mensaje, reconoció el apoyo de todas las organizaciones autónomas  y políticas para poder elevar la calidad de los procesos democráticos y renovar la forma de hacer política en nuestra entidad.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          En la mesa de honor, estuvieron presentes también los Comisionados Carlos Pavón Durán y Aldrin Martín Briceño Conrado, así como los firmantes del convenio de colaboración representantes de los partidos políticos: PAN, Movimiento Ciudadano, Morena, Nueva Alianza Yucatán, PRI, PRD, PT, PVEM, Encuentro Solidario, Redes Sociales Progresistas y Fuerza México.
