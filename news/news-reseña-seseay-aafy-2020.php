				<h2 class="title text-blue_1">Reseña del Foro Virtual “Mano a mano combatiendo la corrupción” en trabajo conjunto con la Agencia de Administración Fiscal de Yucatán (AAFY)</h2>
			</div>
			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
					 Los días 20 y 27 de agosto del presente año, en punto de las 17:00 hrs. en modalidad virtual vía Facebook live SESEAY, se llevaron a cabo las charlas del foro: “Mano a mano, combatiendo la corrupción”, en las que el Lic. César Martínez Paredes, jefe de recomendaciones del SESEAY, adscrito al Departamento Jurídico de la SESEAY, tuvo la oportunidad de dirigirse a personal directivo, administrativo y operativo de ambas instancias, con una audiencia de 82 personas en ambas sesiones el ponente detalló las diversas maneras en las que la corrupción se presenta, repasó uno a uno cada modalidad en la que se ve representado este fenómeno, recalcando las responsabilidades administrativas e incluso legales a las que, en caso de incurrir en alguna de ellas, se haría acreedor como servidor público.   
				</p>
				<p class="paragraph-md text-blue_1 p-2">
          Evento moderado por el Lic. Luis Guillermo Juárez Ramírez, jefe del Departamento de Comunicación de la SESEAY, explicó el objetivo del foro: “Brindar herramientas para reducir riesgos de control interno en servidores públicos, y que estos a su vez tomen conciencia de las consecuencias que se generan al incurrir en acciones que pudieran ser catalogadas como faltas administrativas o hechos de corrupción”.
				</p>
        <p class="paragraph-md text-blue_1 p-2">
          Durante la sesión se pudieron presentar un par de videos alusivos a la manera en como opera el Sistema Estatal Anticorrupción y en que la que se presenta como la corrupción afecta la vida cotidiana de cada ciudadano, aportando además propuestas para superar y evitar estas prácticas.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Cabe destacar que en ambas sesiones estuvieron presentes integrantes de ambas direcciones, así como integrantes del Comité de Participación Ciudadana y personal de la SESEAY.
        </p>
         <p class="paragraph-md text-blue_1 p-2">
         Por último, la SESEAY agradece la disposición y apertura para la realización de este evento a las autoridades y personal administrativo de la Agencia de Administración Fiscal de Yucatán, especialmente al C.P. Juan Carlos Rosel Flores, Director de la entidad y a la C.P. Nelly Castro, del departamento de Administración de personal.
        </p>
