      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-4">
             Este viernes 04 de diciembre de 2020, en punto de las 11:00 horas vía ZOOM, se llevó a cabo la instalación del Comité de Ética Estudiantil en la Universidad Vizcaya de las Américas, con la presencia de los 21 alumnos que integran éste Comité, así como los docentes y administrativos que lo complementan, se dio inicio al acto protocolario, con la presencia de la Presidente del Comité de Participación Ciudadana del Sistema Estatal Anticorrupción de Yucatán, la M. en F. Mónica Alicia Febles Álvarez-Icaza, el Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción, Lic. Edwin Manuel Rejón Pacheco, y el Director General de la institución Dr. José de Jesús Palafox Quintero.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Despues del acto protocolario, el director Palafox Quintero dirigió un discurso a la audiencia en la que enfatizó la actitud de los alumnos, docentes y administrativos de participar de forma permanente en la constitución de dicho Comité, instó a ambas instituciones a no dejar que sea un esfuerzo en vano, a colaborar de manera periódica en los trabajos que sean necesarios para el logro de objetivos, agradeció a los titulas del SEAY la confianza en la institución educatica que representa.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Fue el turno de la Presidente del CPC, la Mtra. Febles, la cual en su mensaje se dirigió primordialmente a todos los involucrados en los procesos, princialmente a los alumnos, docentes y administrativos de la institución educativa, reconoció que desde que se firmó el convenio de colaboración la actitud que ha asumido la institución ha sido de un compromiso permanente y colaborativo.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-4">
            Por último, y después de realizar su toma de protesta, los alumnos presenciaron la conferencia titulada “La ética en el ejercicio profesional” impartida por el Mtro. Luis Guillermo Juárez Ramírez, titular de la jefatura de comunicación de la SESEAY, en la cual explicó durante su charla la importancia de ser íntegros y sobre la responsabilidad social que adquieren al formar parte de dicho Comité. Cabe destacar la actitud participativa y proactiva por parte de los alumnos que interactuaron durante todo el evento.
        </p>
