        <p class="paragraph-md text-blue_1 p-2">
            En punto de las 12:00 p.m. este viernes 15 de noviembre de 2019, en el auditorio de la Universidad de Oriente, se llevó a cabo la instauración del primer Comité de Ética de todo el país, organizado en su totalidad por alumnos de las diversas carreras que se imparten en la Universidad de Oriente (UNO) de Valladolid.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
           Después de llevar a cabo el acto protocolario de bienvenida por parte de la Universidad y presentar a las autoridades presentes: 
        </p>
        <ol class="paragraph-md w-90 text-blue_1 p-2">
            <li>C.P Amireyoly Burgos Aguilar, Directora de Planeación Institucional de la Universidad de Oriente.</li>
            <li>Lic. Edwin Manuel Rejón Pacheco, Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán.</li>
            <li>Mtra. Laura Espadas Herrera, Directora de Vinculación Interinstitucional de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán.</li>
        </ol>
        <p class="paragraph-md text-blue_1 p-2">
            En primera instancia se presentó al Mtro. Luis Guillermo Juárez Ramírez, por parte de la SESEAY, en cual impartió la conferencia “Hablemos de corrupción”, en esta, el conferencista compartió con los alumnos presentes reflexiones y análisis críticos sobre las consecuencias y repercusiones de llevar a cabo conductas deshonestas e inapropiadas relacionadas a la corrupción, con algunas anécdotas e historia de algunos casos reflexionaron sobre la formación integral universitaria. Posteriormente a la conferencia se dio paso a la instalación del Comité de ética de la Universidad, uno a uno las autoridades llamaron a los estudiantes los cuales rindieron protesta como parte de este Comité y en el marco de un discurso por parte del Lic. Rejón Pacheco, los estudiantes se comprometieron a ser un ejemplo de conducta, honestidad y civilidad para todos sus compañeros.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
           Como una acción sin precedente en el Estado, enlistamos a los alumnos que forman parte del primer Comité de Ética universitario de la entidad:
        </p>
        <ol class="paragraph-md w-90 text-blue_1 p-2">
            <li>Fermín de Jesús Castillo Castro - Administración Pública </li>
            <li>Rebeca de Los Ángeles Tun Tuz - Administración Pública </li>
            <li>Estephani Raquel Suárez Torres - Desarrollo Turístico </li>
            <li>Diana Guadalupe Ayuso Poot - Desarrollo Turístico </li>
            <li>Abraham Soberanes Zerón - Bibliotecología y Gestión de la Información </li>
            <li>Carlos Daniel Chooc Cen - Mercadotecnia </li>
            <li>Vanessa Lizeth Sánchez Hernández</li>
        </ol>
        <p class="paragraph-md text-blue_1 p-2">
           Los alumnos expresaron su emoción, y mencionaron que para ellos es una enorme responsabilidad que aceptan con honor, queda claro que para el Sistema Estatal Anticorrupción este es un ejemplo y muestra de gran sentido de responsabilidad al aceptar tan loable distinción. 
        </p>
        <p class="paragraph-md text-blue_1 p-2">
           La Universidad de Oriente en Valladolid, sus estudiantes y autoridades sientan un precedente importante en la consolidación de una sociedad más responsable y consciente de la necesidad de justicia y honestidad.
