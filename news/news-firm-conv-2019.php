        <p class="paragraph-md text-blue_1 p-2">
             El área de Vinculación Interinstitucional, de acuerdo a lo estipulado en el artículo 27 de nuestros estatutos orgánicos, es el área de la Secretaría Ejecutiva encargada de establecer los lineamientos de comunicación y promoción, con el fin de informar a la sociedad de las actividades que se realicen, mantener comunicación e interacción con los diferentes entes estatales y municipales, organizaciones civiles, académicas y las diversas Cámaras, así como con el Sistema Nacional Anticorrupción. Por lo cual se desarrollaron diversas actividades dando cumplimiento a ello. Comenzando con los enlaces formales con las instancias vía firma de convenios, entablando acuerdos para trabajos en conjunto. <br>
             Con los primeros 7 convenios firmados, se han desarrollado, algunas actividades como el desarrollo de un diagnóstico para la generación de recomendaciones no vinculantes referentes a contrataciones y padrón de proveedores de instancias gubernamentales.
             Con el Colegio de contadores, hemos desarrollado capacitaciones como la que realizamos en coordinación con el Centro de Estudios del Mayab, entre los meses mayo y junio. <br>
            Con la Asociación de Scouts México, A.C. realizamos el curso de capacitación “Scouts contra la corrupción”, desarrollado entre los meses abril mayo del presente año, en coordinación con el equipo de Análisis y Políticas Públicas. <br>
            Por otra parte, con el Consejo de Notarios, en colaboración con el Comité de Participación Ciudadana y la Secretaría Ejecutiva del Sistema Estatal Anticorrupción, llevamos a cabo la implementación del proyecto Firmamex, lanzado por el Sistema Nacional Anticorrupción, con el objetivo de impulsar dicha plataforma informática “ABOGACÍA ÉTICA”, invitando a todos los abogados de la entidad a sumarse en esta iniciativa de firmar y hacer constancia de su interés por coadyuvar a un ejercicio profesional ético, respetuoso y responsable, a la vez que mencionó que este es el primer paso a que profesionistas de otras profesiones se vayan sumando de manera paulatina a esta iniciativa. <br>
            En general, con varias de las instancias se han desarrollado serie de pláticas y capacitación en relación con prevención de la corrupción e información general del Sistema Estatal Anticorrupción de Yucatán. Se ha trabajado en las últimas semanas de la mano con la UADY, directamente con sus bachilleratos Preparatoria Uno, Preparatoria Dos y Unidad Académica de Bachillerato con Interacción Comunitaria UABIC, impartiendo pláticas “¿Qué onda con la Corrupción?”, concientizando a alrededor de 1500 jóvenes de terceros grados en la temática. <br>
            De igual manera se estuvo realizando difusión en diversos medios de comunicación, brindando aproximadamente 10 entrevistas en varios programas de radio y televisión, espacios brindados por parte de Grupo SIPSE Radio y Televisión, Periódico La Verdad, Periódico Por Esto, Diario Yucatán, Grupo Megamedia, El mundo de las marcas de RADIO FÓRMULA, Radio FM, entre otros.
