			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
            El día de hoy 18 de septiembre de 2019, en punto de las 10 de la mañana en las instalaciones de la Universidad Tecnológica Metropolitana, se celebró la firma del Convenio de Colaboración entre la propia institución y la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY).
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					 En un breve acto protocolario, los firmantes de este importante documento, El Mtro. Mauricio Cámara Leal, Rector de la Universidad Tecnológica Metropolitana, el Lic. Edwin Manuel Rejón Pacheco, Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY) y la M. en D. Graciela Alejandra Torres Garma, Presidente del Sistema Estatal Anticorrupción de Yucatán, coincidieron en la importancia y relevancia de consolidar este importante compromiso, en coadyuvar en armonía por el desarrollo y promover una sociedad más incluyente y participativa en temas relacionados en la lucha contra la corrupción.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					En un breve acto protocolario, los firmantes de este importante documento, El Mtro. Mauricio Cámara Leal, Rector de la Universidad Tecnológica Metropolitana, el Lic. Edwin Manuel Rejón Pacheco, Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY) y la M. en D. Graciela Alejandra Torres Garma, Presidente del Sistema Estatal Anticorrupción de Yucatán, coincidieron en la importancia y relevancia de consolidar este importante compromiso, en coadyuvar en armonía por el desarrollo y promover una sociedad más incluyente y participativa en temas relacionados en la lucha contra la corrupción.
				</p>
        <p class="paragraph-md text-blue_1 p-2">
          Rendón, integrante del Comité de Participación Ciudadana, y por la SESEAY, la Directora de Vinculación Interinstitucional, la MADN Laura Espadas Herrera.
          El fenómeno de la corrupción se ha convertido en un cáncer que sin lugar a dudas afecta la democracia, la confianza y la credibilidad en las instituciones del Estado, merma nuestra economía e inhibe el crecimiento, maximiza las desigualdades sociales e indudablemente impide el progreso de nuestro país.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Es por ello, que el Sistema Estatal Anticorrupción de Yucatán, se origina como la instancia que tiene por objeto, establecer, articular y evaluar la política estatal en materia de prevención, detección y sanción de responsabilidades administrativas y hechos de corrupción.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Sabemos que la Universidad Tecnológica Metropolitana realiza un esfuerzo constante por ser una institución educativa cercana al estudiante, reconocemos su compromiso por brindar una formación integral a sus alumnos, pero también por ser un ejemplo de transparencia en sus procesos administrativos, nos unimos al esfuerzo de mantenernos en ese tenor.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          El Sistema Estatal Anticorrupción tiene la firme convicción de unir en un esfuerzo sinérgico a todos los sectores y actores que van a permitir que la corrupción se vea mermada y así avanzar en la creación de una sociedad más justa, más equitativa, más funcional.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Cabe recordar que la lucha contra la corrupción no tiene caducidad, los esfuerzos que realicemos todos los actores involucrados deben dar como resultado una sinergia hoy muy necesaria, considerando la exigencia de una sociedad que requiere y merece resultados.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          La consolidación de estas alianzas y el establecimiento de esfuerzos conjuntos entre instancias complementarias no deben quedarse en solo buenas intenciones, deben representarse en acciones que beneficien a nuestra sociedad y que den coherencia a un discurso en el que la transparencia, la honestidad, la solidaridad y empatía estén presentes en todo momento.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Universidad Tecnológica Metropolitana y Sistema Estatal Anticorrupción, seamos un binomio de colaboración permanente, la corrupción es una enfermedad que debemos tratar con toda seriedad, un cuerpo enfermo no rinde.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Estamos seguros que este convenio será el inicio de una serie de acciones conjuntas encaminadas a preservar este status de bienestar y estabilidad social, si bien la corrupción es un fenómeno que hoy se convierte en un reto muy importante a atacar, también es muy cierto que no es imposible contrarrestar sus efectos en el plano inmediato, debe ser un proceso permanente que se arraigue en el tejido social hasta que estas prácticas se conviertan en un hábito cultural.
        </p>
         <p class="paragraph-md text-blue_1 p-2">
           Ambas instancias refrendaron su compromiso de participar activamente llevando a cabo acciones concretas y también a colaborar en la semana cultural que se llevará a cabo en las primeras semanas del mes de diciembre.
        </p>
         <p class="paragraph-md text-blue_1 p-2">
          Se agradece al Mtro. Mauricio Cámara Leal y a su administración, la disposición y compromiso para trabajar de manera conjunta, un esfuerzo que se verá reflejado en un beneficio que coadyuve en una estabilidad de todos los estudiantes de esta universidad, su planta docente, administrativa y de todos los yucatecos.
        </p>
