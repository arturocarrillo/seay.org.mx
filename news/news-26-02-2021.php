        <h2 class="text-md text-green-s1 text-justify font-weight-bold ml-auto mr-auto mt-4 mb-2 p-2">
          FIRMA DE CONVENIO DE COLABORACIÓN ENTRE EL INSTITUTO DE DESARROLLO REGIONAL Y MUNICIPAL (INDERM) Y LA SESEAY.
        </h2>
      </div>
      <div class="w-100 d-flex flex-wrap p-2">
        <p class="text-md text-green-s1 font-weight-normal p-2">
         Este viernes 26 de febrero de 2021, de manera virtual, se llevó a cabo la firma del convenio de colaboración entre el Instituto de Desarrollo Regional y Municipal (INDERM) y la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán (SESEAY). 
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Después de llevar a cabo el acto protocolario de bienvenida, se procedió a presentar a los miembros de la mesa de honor: 
        </p>
        <ol class="w-90 text-md text-green-s1 font-weight-normal m-auto p-2">
            <li>Lic. Ricardo Alcocer Zapata, Director General del Instituto de Desarrollo Regional y Municipal (INDERM).</li>
            <li> M. F. Mónica Alicia Febles Álvarez - Icaza, presidente del Comité de Participación Ciudadana del Sistema Estatal Anticorrupción de Yucatán.</li>
            <li>Lic. Edwin Manuel Rejón Pacheco, Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán.</li>
            <li>Lic. Laura Espadas Herrera, directora de Vinculación Interinstitucional de la SESEAY.</li>
        </ol>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          En primera instancia se otorgó la palabra a la Mtra. Febles, la cual reconoció el trabajo conjunto y la disposición que en todo momento ha mostrado el INDERM para celebrar la firma de dicho documento, mencionó los retos que la situación pandémica nos ha dejado a todos, y enfatizó el doble esfuerzo que aún con esta situación, las instancias han colaborado para que los procesos continúen funcionando, instó a las dos instituciones a colaborar de manera conjunta, a mantenerse en constante comunicación y con la misma actitud colaborativa. Agradeció al Lic. Alcocer toda su disposición y le deseó suerte en su nueva encomienda.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Por su parte, el Lic. Alcocer Zapata, mencionó los objetivos de la celebración del Convenio; establecer las bases de colaboración y mecanismos de coordinación, conforme a los cuales la SESEAY y el Instituto de Desarrollo Regional y Municipal desarrollarán de manera coordinada en programas de capacitación dirigidos a los ayuntamientos. Recordó que uno de los objetivos más importantes es el de intercambiar información y herramientas que sean útiles para el cumplimiento de los objetivos de ambas instancias.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Acto seguido, se procedió a la firma del convenio. El maestro de ceremonias procedió a agradecer y despedir a todos los presentes, una vez que se realizó la toma de la fotografía correspondiente con el Convenio firmado.
        </p>
