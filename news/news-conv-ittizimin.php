				<p class="paragraph-md text-blue_1 p-2">
					Lunes 19 de agosto de 2019, en punto de las 10:00 hrs, en las instalaciones del Instituto Tecnológico de Tizimín, se llevó a cabo la firma del convenio de colaboración entre el propio Instituto y la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
          Después de llevar a cabo el acto cívico de honores a la bandera y con un polifuncional completamente lleno dió inicio la inauguración del nuevo ciclo escolar del Instituto, se proyectó un video del Director General de Institutos Tecnológicos del país, el Dr. Enrique Fernández Fassnacht; acto seguido fue el turno del Director del Centro educativo, el Lic. José Antonio Canto Esquivel, el cual se dirigió a los estudiantes de nuevo ingreso ahí presentes, autoridades federales, municipales y locales refrendando su  compromiso con el desarrollo integral de los estudiantes, se procedió a reconocer el aprovechamiento de los estudiantes más destacados a los cuales se les otorgó un reconocimiento público y por escrito.
				</p>
        <p class="paragraph-md text-blue_1 p-2">
         Fue turno del alcalde de Tizimín el Lic. Mario González González, el cual refrendó su compromiso con la ciudadanía y los proyectos globales y estructurales del estado y particularmente con la creación de un “domo” para la institución, el cual será construido antes de que termine su gestión; se brindó un reconocimiento por una trayectoria docente de 40 años al Mtro. Carlos Arturo Victoria Graniel, el Lic. Joaquín Díaz Mena, representante a nivel federal refrendó el mensaje del presidente de la república apoyando los proyectos de desarrollo educativo del país; y para finalizar el Director del plantel del ITT, el Lic. José Antonio Canto Esquivel, la presidenta del Sistema Estatal Anticorrupción, la M. en D. Graciela Alejandra Torres Garma y el Secretario Técnico de la SESEAY, el Lic. Edwin Manuel Rejón Pacheco, firmaron un convenio de colaboración entre ambas instituciones, quedando patente el compromiso del Sistema Estatal Anticorrupción de colaborar activamente con los Centros Educativos del Estado y especialmente con los del interior de la entidad.
        </p>
