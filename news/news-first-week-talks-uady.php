					  <span class="bold">Reseña de la conferencia: ¿Qué onda con la corrupción? en la Preparatorio No. 3 de la UADY (Universidad Autónoma de Yucatán)</span><br>
            En punto de las 10:10 horas del <span>lunes 23 de septiembre</span> del año en curso, en las instalaciones de la (UABIC) La Unidad Académica Bachillerato con Interacción Comunitaria de la UADY, tuvimos la oportunidad de compartir con los alumnos del quinto semestre de la institución la conferencia ¿Qué onda con la corrupción?; en un extraordinario ambiente, los alumnos escucharon atentos la exposición del Mtro. Luis Guillermo Juárez Ramírez, Jefe del Departamento de Comunicación Social de la SESEAY, en ésta, los alumnos tuvieron la oportunidad de conocer del Sistema Estatal Anticorrupción y su funcionamiento, además de hacer algunas reflexiones en relación a la corrupción y sus implicaciones en su vida personal y profesional. Es importante mencionar que en la ponencia estuvo presente el Dr. José Luis Villamil Urzaiz, integrante del Comité de Participación Ciudadana, y la Mtra. Laura Espadas Herrera, Directora de Vinculación Interinstitucional de la SESEAY.

				</p>
