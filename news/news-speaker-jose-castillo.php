				<h2 class="title text-blue_1">Reseña de la conferencia “Sistema Antisoborno, mejores prácticas” impartida por el Mtro. José López del Castillo.
			</div>
			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
					En punto de las 12:00 horas del jueves 12 de septiembre del año en curso, en las instalaciones de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán, tuvimos la presencia del Mtro. José López del Castillo, Socio Director de LCA Compliance S.C. y miembro del Consejo de Administración y Presidente del Comité Ético NAD Global. Presidente de la Asociación de Agentes Aduanales del Bajío, Miembro de la Asociación de Abogados de empresas, Consejero de Index Querétaro, y profesor invitado a la maestría de Compliance Officer en la Universidad Complutense de Madrid.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					 <span class="bold">“Sistema Antisoborno, mejores prácticas”</span> fue el nombre del tema desarrollado por el ponente, y en el transcurso del mismo expuso los índices de percepción de la corrupción a nivel internacional en el 2018, explicó la importancia de implementar un sistema antisoborno en el sector público, mencionó las características que componen al sistema de gestión antisoborno, sus beneficios y los sectores u organizaciones candidatos a aplicar la norma ISO 37001, hizo hincapié en lo importante que resulta implementar un sistema Antisoborno en el sector privado, y por ultimo explicó los requisitos de la norma y la certificación.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					En la conferencia estuvieron presentes integrantes del Comité de Participación Ciudadana, la Lic. Mónica Febles Álvarez Icaza, quien dirigió el cierre y las palabras de agradecimiento a nuestro invitado, El Arq. Manuel Castillo Rendón, además del personal de la SESEAY, así como personal del despacho Imán & Caro abogados asesores de empresas. Es importante mencionar que la gestión para que la conferencia se llevara a cabo fue coordinada de manera conjunta entre la Asociación Nacional de Abogados de Empresas sección Yucatán (ANADE) y la SESEAY.
				</p>
