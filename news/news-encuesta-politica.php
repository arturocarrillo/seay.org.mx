				<h2 class="title text-blue_1">Reseña de la rueda de prensa para la presentación de la Política Estatal Anticorrupción y la encuesta en línea</h2>
			</div>
			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
            El día de hoy 10 de octubre, del presente año en punto de las 9:00 hrs., en la sala de juntas de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción se presentó ante los medios el primer paso de la Construcción de la Política Estatal Anticorrupción para el Estado de Yucatán.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					 El CPC explicó el primer paso en la construcción de la Política Estatal Anticorrupción, la cual consiste en una consulta en línea dirigida a toda la ciudadanía. Un proceso que tiene por finalidad rescatar la opinión ciudadana en relación a la percepción que se tiene de la corrupción y dar cauce a todas las inquietudes del ciudadano. La importancia de este proceso radica en la creación de una política pública que emerge de la ciudadanía.
				</p>
				<p class="paragraph-md text-blue_1 p-2">
					 La construcción de la Política Estatal Anticorrupción, como hecho inédito, es y será el resultado de una amplia consulta ciudadana en la que el Comité de Participación Ciudadana en conjunto con la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán serán los encargados de administrar la información que el propio ciudadano emitirá, es decir, serán los portavoces de lo que la ciudadanía cada vez más consciente de sus derechos exige a las autoridades. <br>
           Para darle mayor peso a la participación ciudadana en esta tarea, se ha adoptado la misma metodología implementada por el Sistema Nacional Anticorrupción. <br>
           <span>Esta metodología está dividida en 3 fases:</span><br>
           Primera fase: consulta ciudadana. <br> 
           Tiene como objetivo obtener las prioridades de política pública para nuestro Estado que se realizará mediante dos vías.  Por un lado, el día de hoy se anunció el lanzamiento de una Consulta electrónica que se estará haciendo llegar a la mayor cantidad de personas posibles mediante una campaña de redes sociales, acercamiento con organismos sociales, académicos, empresariales, ong’s, grupos de colonos, etc. <br>  
           La segunda fase: elaboración de la propuesta de la política pública. <br>
           En esta fase, se tomará el trabajo realizado en la fase previa para aterrizarlo en un borrador de propuesta para nuestro Estado.  Esta fase la llevará a cabo la Secretaría Ejecutiva del SEAY en conjunto con la Comisión Ejecutiva y un consejo técnico. <br>
           Finalmente, en la tercera fase, se llevarán las propuestas obtenidas de las fases anteriores a la ciudadanía para su retroalimentación final.  Esto se llevará a cabo mediante foros municipales y un consejo consultivo. <br>
           La presentación de esta Política Pública Estatal estuvo a cargo de los integrantes del Comité de Participación Ciudadana, representado en primera instancia por la presidenta del Sistema Estatal Anticorrupción, la M en D Graciela Alejandra Torres Garma, LCC Javier Montes de Oca Zentella, Arq. Manuel J. Castillo Rendón y el Dr. José Luis Villamil Urzaiz y del Secretario Técnico de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción de Yucatán, el Lic Edwin Manuel Rejón Pacheco.


