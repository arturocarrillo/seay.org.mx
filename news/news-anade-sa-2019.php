      <div class="w-100 mt-5 mb-5 flex">
        <p class="paragraph-md text-blue_1 p-2">
         Miércoles 04 de diciembre de 2019, en punto de las 18:00 hrs. en las instalaciones del Rodium Business Center y en el marco de la Semana Anticorrupción se llevó a cabo la conferencia: “Responsabilidad penal de la persona jurídica derivada de la corrupción” por parte de los conferencistas: Abog. Moisés Castro Pizaña y el Abog. Miguel Ángel Hernández de Alba integrantes de la Asociación Nacional de Abogados de Empresas (ANADE), en esta los abogados abordaron el tema de la corrupción desde distintos ámbitos, aportaron datos actuales e ilustrativos sobre la manera en la que la corrupción afecta a las organizaciones y hablaron sobre las responsabilidades que empresarios tienen en sus diferentes interacciones en el entorno profesional y jurídico.
        </p>
        <p class="paragraph-md text-blue_1 p-2">
          Al evento asistió la Presidente del Sistema Estatal Anticorrupción, la Mtra. Graciela Alejandra Torres Garma, la cual al término del evento agradeció la asistencia y participación de los conferencistas, entregó los respectivos reconocimientos y agradeció a toda la asistencia que se congregó en el marco de este magno evento, entre los cuales se encontraban socios de la ANADE, académicos de diversas instituciones educativas, estudiantes y público en general. A su vez, estuvieron presentes Mónica Febles Alvarez Icaza, Javier Montes de Oca Zentella y el Arq. Manuel J. Castillo Rendón, integrantes del Comité de Participación Ciudadana, así como el equipo de Vinculación y la jefatura de Control de Indicadores, de la Secretaría Ejecutiva del Sistema Estatal Anticorrupción.
