        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          La sesión culminó aproximadamente a las 18:30, con la despedida por parte de la Presidenta del SEAY, la Mtra. Mónica Febles y el agradecimiento a todos los presentes.
