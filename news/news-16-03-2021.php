        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
          Como segundo punto a tratar, la Mtra. Febles presentó ante todos los presentes el observatorio electoral ILEJ, el cual, en un trabajo conjunto con el INAIP, y el IEPAC, el Comité de Participación Ciudadana presentará el día de mañana 17 de marzo en una rueda de prensa los pormenores de esta plataforma en la que los ciudadanos podrán revisar las cuentas públicas de los candidatos, y de manera transparente puedan acceder a esta información de importancia pública. Los integrantes del Consejo, pudieron emitir su punto de vista en cuanto a lo presentado y opinando sobre este esfuerzo conjunto entre estos tres entes.
        </p>
        <p class="text-md text-green-s1 font-weight-normal p-2">
