        <h2 class="title text-blue_1">Jóvenes piden participación para frenar la corrupción</h2>
      </div>
      <div class="w-100 mt-5 mb-5 flex">
        <p class="paragraph-md text-blue_1 p-2">
          Mérida, Yucatán, a 5 de diciembre de 2019.- La corrupción está presente en instituciones gubernamentales, en el sector empresarial, organizaciones sindicales y en diferentes esferas de nuestra sociedad, por lo que es obligación de la ciudadanía y de las autoridades tomar acciones conjuntas para combatirla, porque este flagelo no forma parte de nuestra cultura y hay que dejar de normalizarla, concluyeron estudiantes de la Universidad Autónoma de Yucatán (UADY). <br>
          Alumnos de la Facultad de Derecho de la UADY participaron en durante el coloquio “Hablemos de Anticorrupción”, que se realizó durante los ejercicios de reflexión de la Semana Anticorrupción en Yucatán. <br>
          Las y los alumnos Cecilia Patiño Cerón, Víctor Ávila Gil, Janet Uc Sabido, Nashim Avilés González, Ana Laura Moreno Méndez y Roel Mendoza Santiago, cuestionaron, debatieron y aportaron ideas que pueden contribuir en la prevención de este delito que afecta a nuestra sociedad. <br>
          En el encuentro académico, destacaron la importancia de acabar con la impunidad, que es producto de los actos de corrupción, que genera desigualdad e impide el acceso a oportunidades económicas, culturales, sociales y de bienestar. <br>
          En ese tenor, apuntaron que se requiere fomentar la cultura de la legalidad y difundir cuáles son los mecanismos de denuncia a la que pueden recurrir los ciudadanos cuando ocurran ese tipo de hechos. <br>
          Una de las premisas del debate de las y los jóvenes, es que la corrupción no es un elemento de la cultura mexicana y que no está en nuestros genes, pero sí es necesario visibilizar que existe en distintas organizaciones, tanto gubernamentales como empresariales y sindicales. <br>
          Asimismo, se puso sobre la mesa que es una obligación bilateral, entre ciudadanía y gobierno, crear las condiciones necesarias para combatir este flagelo, fortaleciendo instituciones, dotando de autonomía real a aquellas encargadas de investigar y sancionar estos hechos delictivos, y difundiendo la cultura de la legalidad y el Estado de Derecho. <br>
          En ese punto, se habló del trabajo que realiza el Sistema Estatal Anticorrupción en Yucatán, el cual cuenta precisamente con un Comité de Participación Ciudadana que trabaja de manera activa con distintas acciones preventivas y da seguimiento a las quejas que interponen los yucatecos. <br>  
          Eso sí, reconocieron que la corrupción es una cáncer que impacta a diversos sectores del país, por lo que de no tomar medidas para combatirlo continuará afectando nuestra economía, seguridad, medioambiente, salud y derechos humanos. <br>
          Previamente al coloquio, el Sistema Estatal Anticorrupción organizó la mesa panel “Reunión pública con universitarios”, ejercicio en la que jóvenes de diversas instancias educativas compartieron sus reflexiones sobre cómo perciben este fenómeno y qué se puede hacer para erradicarlo. <br>
          En el encuentro, que se transmitió en vivo en las redes sociales, participaron Marcela Palomo Martin, María Luisa Ríos García Bastarrachea, Edwin Moreno Sosaya, Julián Medina y Andrea Sabah Vargas, quienes hicieron énfasis en que hay que dejar de normalizar prácticas de corrupción. <br>
          Dijeron que si los jóvenes no son parte de la solución se convierten en parte del problema, por lo que no pueden ser pasivos ante estos hechos que impactan a terceras personas. <br> 
          También plantearon que estos actos delictivos deben ser sancionados correctamente y que exista una verdadera estrategia de promoción de valores y principios para que nadie incurra en éstos, pero sobre todo, difundir cuáles son sus consecuencias. <br>
          En los eventos de esta jornada estuvieron presentes las y los integrantes del Comité de Participación Ciudadana: presidenta Graciela Alejandra Torres Garma, Mónica Febles Álvarez Icaza, Javier Montes de Oca Zentella, Manuel Castillo Rendón y José Luis Villamil Urzáiz, así como el secretario ejecutivo del Sistema Estatal Anticorrupción, Edwin Rejón Pacheco. 
        </p>
