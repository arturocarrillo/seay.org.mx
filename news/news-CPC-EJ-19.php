			<div class="w-100 mt-5 mb-5 flex">
				<p class="paragraph-md text-blue_1 p-2">
					<span>Antecedentes</span><br>
					Inicio de gestión 2019<br>
					El Comité de Participación Ciudadana (CPC) del Sistema Estatal
					Anticorrupción de Yucatán (SEAY), fue designado el 19 de diciembre de 2017. <br>
					Se conformó por los siguientes ciudadanos :
				</p>
				<ol class="paragraph-md w-90 text-blue_1 p-2">
					<li>Eduardo Antonio Ancona González</li>
					<li>Graciela Alejandra Torres Garma</li>
					<li>Mónica Alicia Febles Alvarez-Icaza</li>
					<li>Javier Montes de Oca Zentella</li>
					<li>Manuel Jesús Castillo Rendón</li>
				</ol>
				<p class="paragraph-md text-blue_1 p-2">
					Del 19 de diciembre de 2017 al 31 de marzo de 2019 el ciudadano Eduardo Antonio Ancona González ocupó la presidencia del CPC cargo que a partir del 1º de abril de 2019 y hasta el 31 de marzo de 2020 ocupa Graciela Alejandra Torres Garma.<br>
					Con el objeto de que la ciudadanía tenga mayor conocimiento del CPC presentamos este “Informe de Actividades” del período 1º de abril al 30 de junio de 2019. <br>
					<span>Resumen:</span>
				</p>
				<ol class="paragraph-md w-90 text-blue_1 p-2">
					<li>Sesiones del CPC <span>13</span></li>
					<li>Sesiones de la Comisión Ejecutiva <span>10</span></li>
					<li>Sersiones del Comité Coordinador <span>02</span></li>
					<li>Sesiones Órgano de Gobierno <span>05</span></li>
					<li>Reuniones con terceros <span>14</span></li>
					<li>Participación en eventos <span>06</span></li>
					<li>Acciones mediáticas <span>70</span></li>
					<li>Convenios <span>02</span></li>
				</ol>
				<p class="paragraph-md text-blue_1 p-2">
					<span>Actividades</span><br>
					Sesiones<br>
					En las 23 sesiones que durante el trimestre abril-junio de 2019 realizó el CPC y la Comisión Ejecutiva, resaltamos los principales resultados:
				</p>
				<ul class="paragraph-md w-90 text-blue_1 p-2">
					<li>Toma el cargo como presidente Graciela Torres Garma.</li>
					<li>Toma el cargo como nuevo miembro del CPC José Luis Villamil Urzaiz.</li>
					<li>Se acuerda la utilización del formato 3 de 3 que utiliza la Secretaría de la Contraloría del Estado de Yucatán, en virtud de no estar aún aprobado el formato nacional.</li>
					<li>Basados en el documento del Sistema Nacional Anticorrupción se trabaja en la revisión de ejes y prioridades para integrar el proyecto de políticas públicas anticorrupción para el estado de Yucatán.</li>
					<li>Se ponen las bases y se concreta el Programa de Trabajo del CPC bajo 3 ejes centrales denominado ACI: <br>
					1- Atender la arbitrariedad <br>
						2- Combatir la impunidad <br>
						3- Involucrar a la sociedad <br></li>
					<li>Se toman acuerdos para estrechar vínculos con las instituciones educativas para fortalecer actividades y programas que promuevan la cultura anticorrupción.</li>
					<li>Se coordina con la Secretaría Ejecutiva el acercamiento con los municipios del estado para ofrecerles herramientas para prevenir y combatir faltas administrativas y hechos de corrupción en beneficio de la ciudadadanía.</li>
					<li>Se realizan reuniones con diversos organismos públicos y privados para lograr alianzas o convenios que permitan unir esfuerzos para combatir la corrupción e impunidad.</li>
					<li>Se trabaja en coordinación con el Sistema Nacional Anticorrupción para realizar actividades conjuntas que aporten nuevas formas de prevención y combate a la corrupción.</li>
					<li>Se acuerda hacer del Mecanismo de Queja una herramienta propia del SEAY que el ciudadano pueda utilizar para combatir posibles actos de corrupción que lo afecte. El 25 de junio de 2019 se publica en el Diario Oficial del Estado quedando con esto, ya aprobado por ley.</li>
					<li>Se realiza, en coordinación con la Secretaría Ejecutiva, una revisión a las diversas leyes que inciden en la Ley del Sistema Estatal Anticorrupción para proponer mecanismos y/o adecuaciones que se consideran necesarias para lograr mejoras en la ley. Partimos de la experiencia inédita, desde septiembre de 2018 cuando se integra ya todo el SEAY, del acontecer diario de este Sistema dirigido especialmente al combate de la corrupción.</li>
				</ul>
				<p class="paragraph-md text-blue_1 p-2">
					<span>Actividades</span><br>
					Eventos <br>
					De los 6 eventos realizados durante el trimestre abril-junio de 2019 apuntamos lo siguiente:
				</p>
				<ul class="paragraph-md w-90 text-blue_1 p-2">
					<li>Taller “Corrupción: un vistazo estadístico” impartido por el Vice Presidente de la Junta de Gobierno del INEGI Nacional, Mtro. Adrián Franco Barrios</li>
					<li>Asistencia de la Presidente del CPC a la Reunión Nacional de Presidentes de CPC’s y Secretarios Técnicos, en la ciudad de Villahermosa Tabasco.</li>
					<li>Participación en la inauguración del diplomado “Capacitación Gubernamental” que ofrece el Instituto Mexicano de Contadores Públicos (IMPC) y en la conferencia “Transparencia y Fiscalización Gubernamental” impartida por la próxima presidente del SNA</li>
					<li>Asistencia a la mesa panel “Acciones institucionales en materia del sistema anticorrupción” convocada por el Poder Judicial del Estado y la Vicefiscalía Especializada en el Combate a la Corrupción y presencia, de la presidente del CPC como testigo de honor, en la firma del convenio de colaboración y cooperación de ambas dependencias gubernamentales.</li>
					<li>Asistencia a la conferencia “Vinculación del Sistema Nacional de Transparencia y el Sistema Nacional Anticorrupción” impartida por la Dra Josefina Román Vergara, Comisionada del Instituto Nacional de Acceso a la Información (INAIP)</li>
					<li>Asistencia al evento “Convenio de coordinación para la operación del Programa de Vivienda Social 2019”</li>
				</ul>
				<p class="paragraph-md text-blue_1 p-2">
					<span>Vinculación Ciudadana</span><br>
					Acciones Mediáticas<br>
					De las 70 acciones de comunicación a través de medios masivos que el CPC realizó durante el trimestre abril-junio de 2019, podemos estractar lo siguiente:
				</p>
				<ul class="paragraph-md w-90 text-blue_1 p-2">
					<li>Rueda de prensa en la SESEAY para presentar a la nueva presidente y nuevo consejero ciudadano del CPC.</li>
					<li>Participación en el programa radiofónico “Personajes de Hoy” conducido por Arcadio Huchim y transmitido a través de 93.7 FM La Reverenda.</li>
					<li>Participación en el Noticiero Fórmula Yucatán conducido por José Luis Preciado y transmitido en Radio Fórmula y Tele Fórmula.</li>
					<li>Entrevista para el reportaje “La corrupción genera muerte e impunidad” publicado por el periódico La Verdad de Yucatán.</li>
					<li>Entrevista para los espacios noticiosos de SIPSE TV en sus ediciones matutina, vespertina y nocturna.</li>
					<li>Entrevista para el programa “El Mundo de las Marcas” transmitido por Tele Fórmula y Radio Fórmula.</li>
					<li>Entrevista en la SESEAY con el periodista David Domínguez Massa para la nota periodística “Cita con alcaldes” publicada en el Diario de Yucatán.</li>
					<li>A través de la página @cpcyucatan.org.mx, página oficial en facebook del CPC para vincularse con la sociedad, se realizaron 61 publicaciones, entre notas informativas propias y contenidos compartidos de otros medios, y 2 transmisiones en vivo.</li>
				</ul>
				<p class="paragraph-md text-blue_1 p-2">
					<span>Convenios</span><br> 
					Acuerdos establecidos <br>
					Los dos convenios firmados durante el trimestre abril-junio de 2019 fueron: <br>
				</p>
				<ol class="paragraph-md w-90 text-blue_1 p-2">
					<li>Convenio de Colaboración entre el Sistema Estatal Anticorrupción de Yucatán y la Asociación de Scouts de México, A.C. (ASMAC).</li>
					<li> Convenio de Colaboración entre el Sistema Estatal Anticorrupción de Yucatán y el Ayuntamiento de Mérida.</li>
				</ol>
				<p class="paragraph-md text-blue_1 p-2">
					<span>Destacado</span><br>
					Apunte final <br>
					La vinculación con autoridades académicas del sector educativo para fortalecer la cultura anticorrupción y la puesta en marcha del Mecanismo de Queja son dos aspectos que deseamos destacar. Este último, representa el primer mecanismo legal que un CPC en el país logra establecer, como inédita herramienta, para que un ciudadano pueda responder contra un acto de corrupción que lo afecte. Un mecanismo ciudadano, con validez legal, para defensa del ciudadano. Invitamos a la ciudadanía a utilizar el Mecanismo de Queja, que puede ser incluso de forma anónima, como una legítima defensa ante un atropello de corrupción. 
				</p>
